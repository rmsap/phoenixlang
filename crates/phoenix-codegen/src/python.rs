//! Python code generation for Phoenix Gen.
//!
//! Generates four files from a Phoenix program:
//! - **models.py** — Pydantic `BaseModel` classes for structs, `str` enums,
//!   and derived body types with `Field()` constraint validation.
//! - **client.py** — an async `httpx`-based typed client with methods for
//!   each endpoint.
//! - **handlers.py** — a `Handlers` `Protocol` class that server implementations
//!   must satisfy.
//! - **server.py** — a FastAPI router that wires HTTP routes to handler methods
//!   with automatic parameter parsing and error mapping.

use std::collections::BTreeSet;

use phoenix_parser::ast::{Declaration, EnumDecl, PaginationMode, Program, StructDecl};
use phoenix_sema::Analysis;
use phoenix_sema::checker::{DefaultValue, DerivedField, EndpointInfo};
use phoenix_sema::types::Type;

mod format;
use format::*;

/// The output of Python code generation: four file contents.
pub struct PythonFiles {
    /// Content for `__init__.py` — marks the output dir as a package so the
    /// intra-package relative imports (`from .models import ...`) resolve.
    pub init: String,
    /// Content for `models.py` — Pydantic models, enums, derived body types.
    pub models: String,
    /// Content for `client.py` — async httpx-based client.
    pub client: String,
    /// Content for `handlers.py` — Protocol-based handler interface.
    pub handlers: String,
    /// Content for `server.py` — FastAPI router wiring.
    pub server: String,
}

/// Generates Python code from a parsed and type-checked Phoenix program.
pub fn generate_python(program: &Program, check_result: &Analysis) -> PythonFiles {
    let generator = PyGenerator::new(check_result);
    generator.generate(program)
}

/// Internal Python code generator.
struct PyGenerator<'a> {
    check_result: &'a Analysis,
    models_out: String,
    client_out: String,
    handlers_out: String,
    server_out: String,
    emitted_derived_types: BTreeSet<String>,
}

/// Which model-level type usages drive models.py imports. Bundles the flags
/// `emit_models_imports` consumes so its signature stays legible. Each field is
/// "the schema uses this type somewhere a models.py import is required."
struct ModelsImports {
    datetime: bool,
    uuid: bool,
    decimal: bool,
    money: bool,
    url: bool,
    bytes: bool,
}

impl<'a> PyGenerator<'a> {
    /// Creates a new Python generator with the given semantic analysis results.
    fn new(check_result: &'a Analysis) -> Self {
        Self {
            check_result,
            models_out: String::new(),
            client_out: String::new(),
            handlers_out: String::new(),
            server_out: String::new(),
            emitted_derived_types: BTreeSet::new(),
        }
    }

    /// Generates `models.py`, `client.py`, `handlers.py`, and `server.py`
    /// from the program AST.
    fn generate(mut self, program: &Program) -> PythonFiles {
        // ── models.py ───────────────────────────────────────────────
        self.models_out
            .push_str("# Generated by Phoenix Gen — do not edit manually.\n\n");
        self.models_out
            .push_str("from __future__ import annotations\n\n");
        let models_needs_datetime = self.models_use(type_uses_datetime);
        let models_needs_uuid = self.models_use(type_uses_uuid);
        let models_needs_decimal = self.models_use(type_uses_decimal);
        let needs_money = crate::schema_uses_money(program, self.check_result);
        let needs_url = self.models_use(type_uses_url);
        let needs_bytes = self.models_use(type_uses_bytes);
        self.emit_models_imports(
            program,
            ModelsImports {
                datetime: models_needs_datetime,
                uuid: models_needs_uuid,
                decimal: models_needs_decimal,
                money: needs_money,
                url: needs_url,
                bytes: needs_bytes,
            },
        );
        // The `Url` validated-string alias (`Annotated[str, BeforeValidator(...)]`),
        // emitted before the models that reference it.
        if needs_url {
            self.emit_url_alias();
        }
        // The `Bytes` binary alias (`Annotated[bytes, BeforeValidator, PlainSerializer]`):
        // raw `bytes` at rest, base64 string on the JSON wire.
        if needs_bytes {
            self.emit_bytes_alias();
        }
        // The composite `Money` model (+ its ISO-4217 currency set/validator),
        // once, before the user models that may embed it.
        if needs_money {
            self.emit_money_model();
        }

        // The client-side `FileUpload` helper (filename + content bytes) is shared
        // by every multipart endpoint, so emit it once up front when any exists.
        if self
            .check_result
            .endpoints
            .iter()
            .any(|ep| ep.body_is_multipart)
        {
            self.emit_file_upload_dataclass();
        }

        for decl in &program.declarations {
            match decl {
                Declaration::Struct(s) => self.emit_model(s),
                Declaration::Enum(e) => self.emit_enum(e),
                _ => {}
            }
        }

        for ep in &self.check_result.endpoints {
            self.emit_derived_model(ep);
            self.emit_response_projection_model(ep);
            self.emit_result_envelope(ep);
            self.emit_page_model(ep);
            self.emit_multi_status_envelope(ep);
        }

        // ── client.py ───────────────────────────────────────────────
        self.client_out
            .push_str("# Generated by Phoenix Gen — do not edit manually.\n\n");
        // The client decodes the bare response body via `py_decode_expr` even for
        // a response-header endpoint (it wraps the decoded body in the envelope:
        // `body=datetime.fromisoformat(...)`), so it must NOT exclude those — pass
        // `false`. (The handler, below, returns the named envelope and never
        // decodes, so it keeps `true`.)
        let client_needs_datetime = self.request_input_uses(type_uses_datetime)
            || self.response_header_uses(type_uses_datetime)
            || self.bare_response_uses(false, type_uses_datetime);
        let client_needs_uuid = self.request_input_uses(type_uses_uuid)
            || self.response_header_uses(type_uses_uuid)
            || self.bare_response_uses(false, type_uses_uuid);
        let client_needs_decimal = self.request_input_uses(type_uses_decimal)
            || self.response_header_uses(type_uses_decimal)
            || self.bare_response_uses(false, type_uses_decimal);
        self.emit_client_imports(
            client_needs_datetime,
            client_needs_uuid,
            client_needs_decimal,
        );
        self.client_out.push_str("\n\nclass ApiClient:\n");
        self.client_out.push_str(
            "    def __init__(self, base_url: str) -> None:\n        self.base_url = base_url\n        self.client = httpx.AsyncClient()\n",
        );

        for ep in &self.check_result.endpoints {
            self.emit_client_method(ep);
        }

        // ── handlers.py ─────────────────────────────────────────────
        self.handlers_out
            .push_str("# Generated by Phoenix Gen — implement the handler methods below.\n\n");
        let handlers_needs_datetime = self.request_input_uses(type_uses_datetime)
            || self.bare_response_uses(true, type_uses_datetime);
        let handlers_needs_uuid = self.request_input_uses(type_uses_uuid)
            || self.bare_response_uses(true, type_uses_uuid);
        let handlers_needs_decimal = self.request_input_uses(type_uses_decimal)
            || self.bare_response_uses(true, type_uses_decimal);
        self.emit_handler_imports(
            handlers_needs_datetime,
            handlers_needs_uuid,
            handlers_needs_decimal,
        );
        self.handlers_out
            .push_str("\n\nclass Handlers(Protocol):\n");

        // A schema with no endpoints (a types-only package) yields an empty
        // Protocol body, which is a Python `IndentationError` — emit `pass`.
        if self.check_result.endpoints.is_empty() {
            self.handlers_out.push_str("    pass\n");
        }
        for ep in &self.check_result.endpoints {
            self.emit_handler_method(ep);
        }

        // ── server.py ───────────────────────────────────────────────
        self.server_out
            .push_str("# Generated by Phoenix Gen — do not edit manually.\n\n");
        let server_needs_datetime = self.request_input_uses(type_uses_datetime)
            || self.bare_response_uses(false, type_uses_datetime);
        let server_needs_uuid = self.request_input_uses(type_uses_uuid)
            || self.bare_response_uses(false, type_uses_uuid);
        let server_needs_decimal = self.request_input_uses(type_uses_decimal)
            || self.bare_response_uses(false, type_uses_decimal);
        self.emit_server_imports(
            server_needs_datetime,
            server_needs_uuid,
            server_needs_decimal,
        );
        self.server_out
            .push_str("\n\ndef create_router(handlers: Handlers) -> APIRouter:\n");
        self.server_out.push_str("    router = APIRouter()\n");

        // FastAPI (like Express) matches routes first-registered-wins, so a
        // parametric route (`/api/posts/{id}`) registered before a static sibling
        // (`/api/posts/paged`) would shadow it. Register more-specific (more-
        // static) routes first so literal segments win. Stable sort preserves
        // source order among equally-specific paths. See `route_specificity_key`.
        let mut ordered: Vec<&EndpointInfo> = self.check_result.endpoints.iter().collect();
        ordered.sort_by_key(|ep| crate::route_specificity_key(&ep.path));

        for ep in ordered {
            self.emit_server_route(ep);
        }

        ensure_blank_lines(&mut self.server_out, 1);
        self.server_out.push_str("    return router\n");

        // Normalize models.py's trailing newlines to exactly one. Each model
        // emitter prepends its own leading blank lines (via `ensure_blank_lines`),
        // so a block that emits trailing spacer blanks for a *following* model —
        // notably `emit_money_model` — leaves dangling blank lines when it is the
        // file's last definition (a `Money`-only schema with no user models). black
        // and ruff reject trailing blank lines; this is idempotent when a user
        // model already terminates the file with a single newline.
        while self.models_out.ends_with('\n') {
            self.models_out.pop();
        }
        self.models_out.push('\n');

        PythonFiles {
            init: "# Generated by Phoenix Gen — do not edit manually.\n".to_string(),
            models: self.models_out,
            client: self.client_out,
            handlers: self.handlers_out,
            server: self.server_out,
        }
    }

    // ── `datetime` import detection ─────────────────────────────────
    // A `DateTime` field/param renders as the Python `datetime` type, which needs
    // `from datetime import datetime` in the file that names it. Detection is
    // per-file (an unused import trips ruff F401; a missing one trips F821), keyed
    // to the exact positions each file renders.

    // These per-file predicates are generic over a `uses: fn(&Type) -> bool` type
    // test so the SAME position logic serves both special scalars: `DateTime`
    // (`from datetime import datetime`) and `Uuid` (`from uuid import UUID`). Each
    // is evaluated once per scalar; the results drive that scalar's per-file import.

    /// Whether any request query param or request header is (or nests) the scalar
    /// `uses` matches — these surface as annotated params in the client, handler,
    /// and server signatures.
    fn request_input_uses(&self, uses: fn(&Type) -> bool) -> bool {
        self.check_result.endpoints.iter().any(|ep| {
            ep.query_params.iter().any(|qp| uses(&qp.ty)) || ep.headers.iter().any(|h| uses(&h.ty))
        })
    }

    /// Whether any response header is (or nests) the scalar `uses` matches — the
    /// client parses these (`datetime.fromisoformat`/`UUID`) into the result envelope.
    fn response_header_uses(&self, uses: fn(&Type) -> bool) -> bool {
        self.check_result
            .endpoints
            .iter()
            .any(|ep| ep.response_headers.iter().any(|h| uses(&h.ty)))
    }

    /// Whether any endpoint's BARE response (decoded/returned as `ep.response`, not
    /// a named envelope) names the scalar `uses` matches — e.g. `response Uuid` /
    /// `response List<DateTime>`. Only the HANDLER renders the named
    /// `<Endpoint>Result` envelope for response-header endpoints, so only it
    /// excludes those (`exclude_header_endpoints = true`); the client decodes the
    /// bare body into that envelope's `body=` and the server returns the bare
    /// `result.body`, so both pass `false`. Binary, multi-status, and pagination
    /// render a NAME, not the bare type, so they never count.
    fn bare_response_uses(&self, exclude_header_endpoints: bool, uses: fn(&Type) -> bool) -> bool {
        self.check_result.endpoints.iter().any(|ep| {
            let renders_bare = !exclude_header_endpoints || ep.response_headers.is_empty();
            ep.response_statuses.is_empty()
                && !ep.response_is_binary
                && ep.pagination.is_none()
                && renders_bare
                && ep.response.as_ref().is_some_and(&uses)
        })
    }

    /// Whether models.py names the scalar `uses` matches: any non-file-bearing
    /// struct field, any non-multipart derived body field, any response-header
    /// envelope field or its bare `body` field, or a pagination page's `items`.
    fn models_use(&self, uses: fn(&Type) -> bool) -> bool {
        // `structs_with_names` is the resolved-module struct set (StructId order),
        // which is wider than the user-declared `Declaration::Struct`s — it also
        // includes derived `<Endpoint>Body` structs. That's intentional and
        // harmless here: derived bodies are emitted into models.py too, and the
        // import decision is an OR (a uuid-bearing body is also caught by
        // `body_field`), so widening the struct set can never wrongly omit an
        // import, only redundantly confirm one.
        let struct_field = self
            .check_result
            .module
            .structs_with_names()
            .any(|(_, _, info)| !info.is_file_bearing && info.fields.iter().any(|f| uses(&f.ty)));
        let body_field = self.check_result.endpoints.iter().any(|ep| {
            !ep.body_is_multipart
                && ep
                    .body
                    .as_ref()
                    .is_some_and(|b| b.fields.iter().any(|f| uses(&f.ty)))
        });
        let envelope_body =
            self.check_result.endpoints.iter().any(|ep| {
                !ep.response_headers.is_empty() && ep.response.as_ref().is_some_and(&uses)
            });
        let page_items = self.check_result.endpoints.iter().any(|ep| {
            ep.pagination
                .as_ref()
                .is_some_and(|pag| uses(&pag.item_type))
        });
        struct_field || body_field || envelope_body || page_items || self.response_header_uses(uses)
    }

    /// Emits the `Url` validated-string alias into models.py: a `_validate_url`
    /// function (rejects a scheme-less string) and `Url = Annotated[str,
    /// BeforeValidator(_validate_url)]`. The value stays a plain `str` (validated,
    /// NOT normalized, so it round-trips exactly); pydantic runs the validator on
    /// model fields and FastAPI runs it on `Url` query/header params.
    fn emit_url_alias(&mut self) {
        // Opens with a `def` (not an assignment), so 2 blank lines after imports.
        ensure_blank_lines(&mut self.models_out, 2);
        // The `isinstance` guard keeps a non-string JSON value (e.g. a number) on
        // the 422 path: as a `BeforeValidator` this runs ahead of the `str` schema,
        // so a bare `urlparse(value)` would raise `TypeError` — which pydantic does
        // NOT treat as a validation error — and surface as a 500.
        self.models_out.push_str(
            "def _validate_url(value: object) -> str:\n    \
             \"\"\"Validate that `value` is an absolute URL string (has a scheme).\"\"\"\n    \
             if not isinstance(value, str):\n        \
             raise ValueError(f\"invalid URL: {value!r}\")\n    \
             if not urlparse(value).scheme:\n        \
             raise ValueError(f\"invalid URL: {value}\")\n    \
             return value\n\n\n\
             Url = Annotated[str, BeforeValidator(_validate_url)]\n",
        );
    }

    /// Emits the `Bytes` binary alias into models.py: `_bytes_from_b64` (decodes a
    /// base64 wire string to raw `bytes`, passing raw `bytes` through unchanged so
    /// a caller works with binary directly) + `_bytes_to_b64` (encodes raw `bytes`
    /// to base64), wired as `Bytes = Annotated[bytes, BeforeValidator(...),
    /// PlainSerializer(...)]`. The runtime value is real `bytes` (a first-class
    /// binary value, NOT a base64 string); the JSON wire carries base64. pydantic
    /// runs the validator on parse and the serializer on `model_dump`/dump-json.
    fn emit_bytes_alias(&mut self) {
        // Opens with a `def` (not an assignment), so 2 blank lines after imports.
        ensure_blank_lines(&mut self.models_out, 2);
        self.models_out.push_str(
            "def _bytes_from_b64(value: bytes | str) -> bytes:\n    \
             \"\"\"Decode a base64 wire string to raw bytes; pass raw bytes through.\"\"\"\n    \
             if isinstance(value, str):\n        \
             return base64.b64decode(value)\n    \
             return value\n\n\n\
             def _bytes_to_b64(value: bytes) -> str:\n    \
             \"\"\"Encode raw bytes to a base64 wire string.\"\"\"\n    \
             return base64.b64encode(value).decode()\n\n\n\
             Bytes = Annotated[\n    \
             bytes,\n    \
             BeforeValidator(_bytes_from_b64),\n    \
             PlainSerializer(_bytes_to_b64, return_type=str),\n\
             ]\n",
        );
    }

    /// Emits the composite `Money` pydantic model into models.py: the ISO-4217
    /// `_CURRENCY_CODES` set, then `class Money(BaseModel)` with a `Decimal`
    /// `amount`, a `str` `currency`, and a `field_validator` rejecting non-ISO-4217
    /// currencies. pydantic handles decode/serialization (amount ↔ string).
    fn emit_money_model(&mut self) {
        // One blank line after the import block (isort `lines-after-imports`): the
        // Money block opens with an ASSIGNMENT (`_CURRENCY_CODES`), not a def, so
        // ruff wants 1 here — the 2-blank rule is only before a class/function.
        ensure_blank_lines(&mut self.models_out, 1);
        self.models_out.push_str("_CURRENCY_CODES = {\n");
        for code in crate::iso4217::ISO_4217_CODES {
            self.models_out.push_str(&format!("    \"{code}\",\n"));
        }
        self.models_out.push_str("}\n\n\n");
        self.models_out.push_str(
            "class Money(BaseModel):\n    \
             \"\"\"A monetary amount: an exact decimal plus an ISO-4217 currency code.\"\"\"\n\n    \
             amount: Decimal\n    \
             currency: str\n\n    \
             @field_validator(\"currency\")\n    \
             @classmethod\n    \
             def _validate_currency(cls, v: str) -> str:\n        \
             if v not in _CURRENCY_CODES:\n            \
             raise ValueError(f\"invalid ISO 4217 currency code: {v}\")\n        \
             return v\n\n\n",
        );
    }

    // ── models.py emission ──────────────────────────────────────────

    /// Emits imports needed by models.py based on what types are used. Each field
    /// of [`ModelsImports`] flags "the schema uses this type somewhere a models.py
    /// import is required."
    fn emit_models_imports(&mut self, program: &Program, needs: ModelsImports) {
        let ModelsImports {
            datetime: needs_datetime,
            uuid: needs_uuid,
            decimal: needs_decimal,
            money: needs_money,
            url: needs_url,
            bytes: needs_bytes,
        } = needs;
        // `Money`'s generated model uses `Decimal` (its `amount`) and subclasses
        // `BaseModel`, so a Money-using schema needs both even if no user field is
        // a bare `Decimal`.
        let needs_decimal = needs_decimal || needs_money;
        // `needs_validator`: any emitted model has a `where`-constrained field, so it
        // gets a `@model_validator(mode="after")` enforcing the full constraint
        // expression → pull in pydantic's `model_validator`.
        let mut needs_validator = false;
        let mut needs_enum = false;
        // `ConfigDict(populate_by_name=True)` is emitted on any model carrying an
        // aliased field — a field whose schema (wire) name differs from its
        // snake_case Python attribute (a camelCase name, or a keyword escaped with a
        // trailing `_`), or a pagination envelope (whose `totalCount`/`nextCursor`
        // always alias). It shares the SAME emit gating as `needs_validator` — same
        // model set, same `is_file_bearing`/`is_multipart` exclusions — so the flags
        // are computed together in one traversal to keep them from drifting from each
        // other or from the emitters. The import must track the emitted
        // `model_config`/`Field(alias=…)`/`model_validator` lines exactly, or ruff
        // flags an unused/undefined name.
        let mut needs_config_dict = false;

        for decl in &program.declarations {
            match decl {
                Declaration::Struct(s) => {
                    // File-bearing structs emit no model (see `emit_model`), so
                    // neither their constraints nor their field names reach the
                    // import gate.
                    if let Some(info) = self.check_result.module.struct_info_by_name(&s.name)
                        && !info.is_file_bearing
                    {
                        if info.fields.iter().any(|f| f.constraint.is_some()) {
                            needs_validator = true;
                        }
                        if info.fields.iter().any(|f| is_aliased(&f.name)) {
                            needs_config_dict = true;
                        }
                    }
                }
                Declaration::Enum(_) => needs_enum = true,
                _ => {}
            }
        }
        // Derived models (request bodies, response projections) and the pagination
        // envelope, mirroring their emitters. Multipart bodies emit no model.
        for ep in &self.check_result.endpoints {
            if !ep.body_is_multipart
                && let Some(ref body) = ep.body
            {
                if body.fields.iter().any(|f| f.constraint.is_some()) {
                    needs_validator = true;
                }
                if body.fields.iter().any(|f| is_aliased(&f.name)) {
                    needs_config_dict = true;
                }
            }
            if let Some(ref proj) = ep.response_projection {
                if proj.fields.iter().any(|f| f.constraint.is_some()) {
                    needs_validator = true;
                }
                if proj.fields.iter().any(|f| is_aliased(&f.name)) {
                    needs_config_dict = true;
                }
            }
            if ep.pagination.is_some() {
                needs_config_dict = true;
            }
        }

        // Whether the schema involves `File` anywhere (a file-bearing struct, a
        // multipart body, or a binary response). When it does NOT, the import
        // logic stays byte-for-byte as before (BaseModel always emitted) so no
        // pre-existing JSON snapshot shifts. Only File-bearing schemas — where a
        // skipped file-bearing struct can leave BaseModel unused — recompute it.
        let involves_file = program.declarations.iter().any(|decl| {
            matches!(decl, Declaration::Struct(s)
                if self
                    .check_result
                    .module
                    .struct_info_by_name(&s.name)
                    .is_some_and(|info| info.is_file_bearing))
        }) || self
            .check_result
            .endpoints
            .iter()
            .any(|ep| ep.body_is_multipart || ep.response_is_binary);

        // A `BaseModel` subclass is emitted for any non-file-bearing struct, any
        // non-multipart derived body, or any response-header envelope. Enums use
        // `Enum`, not `BaseModel`. In a File-involving schema, importing
        // `BaseModel` when nothing subclasses it (e.g. a binary-download-only
        // schema whose sole struct is file-bearing and emits no model) trips ruff
        // F401, so gate the import on actual use.
        let needs_base_model = !involves_file
            || program.declarations.iter().any(|decl| {
                matches!(decl, Declaration::Struct(s)
                    if self
                        .check_result
                        .module
                        .struct_info_by_name(&s.name)
                        .is_none_or(|info| !info.is_file_bearing))
            })
            || self.check_result.endpoints.iter().any(|ep| {
                (!ep.body_is_multipart && ep.body.is_some())
                    || !ep.response_headers.is_empty()
                    || ep.pagination.is_some()
            });

        // A multipart endpoint pulls in the client-side `FileUpload` dataclass
        // (emitted into models.py by `emit_file_upload_dataclass`), which needs
        // `@dataclass`.
        let needs_dataclass = self
            .check_result
            .endpoints
            .iter()
            .any(|ep| ep.body_is_multipart);

        // Import groups, in isort order: standard library, then third party.
        // (`from __future__ import annotations` is emitted separately first.)
        // `dataclasses` sorts before `enum` within the stdlib group.
        let mut stdlib: Vec<&str> = Vec::new();
        // A straight `import` sorts before the `from … import …` block (isort). The
        // `Bytes` alias base64-(de)codes via `base64.b64encode`/`b64decode`.
        if needs_bytes {
            stdlib.push("import base64");
        }
        if needs_dataclass {
            stdlib.push("from dataclasses import dataclass");
        }
        // isort order within the stdlib group:
        // dataclasses < datetime < decimal < enum < uuid.
        if needs_datetime {
            stdlib.push("from datetime import datetime");
        }
        if needs_decimal {
            stdlib.push("from decimal import Decimal");
        }
        if needs_enum {
            stdlib.push("from enum import Enum");
        }
        // isort: `typing` < `urllib` < `uuid`. Both the `Url` and `Bytes` aliases
        // use `Annotated` (typing); `Url` also uses `urlparse` (urllib.parse).
        if needs_url || needs_bytes {
            stdlib.push("from typing import Annotated");
        }
        if needs_url {
            stdlib.push("from urllib.parse import urlparse");
        }
        if needs_uuid {
            stdlib.push("from uuid import UUID");
        }
        if !stdlib.is_empty() {
            self.models_out.push_str(&stdlib.join("\n"));
            self.models_out.push_str("\n\n");
        }
        // pydantic members in ruff's isort `order-by-type` order: the CamelCase
        // classes (BaseModel, BeforeValidator, ConfigDict, Field, PlainSerializer)
        // before the snake_case functions (field_validator, model_validator). `Money`
        // subclasses `BaseModel` and uses `field_validator`; both `Url` and `Bytes`
        // use `BeforeValidator`; `Bytes` also uses `PlainSerializer` (raw `bytes` →
        // base64 wire); `ConfigDict` carries `populate_by_name` on aliased models;
        // `model_validator` enforces `where`-constraints on constrained models.
        let mut pydantic: Vec<&str> = Vec::new();
        if needs_base_model || needs_validator || needs_money {
            pydantic.push("BaseModel");
        }
        if needs_url || needs_bytes {
            pydantic.push("BeforeValidator");
        }
        if needs_config_dict {
            pydantic.push("ConfigDict");
        }
        // `Field` is needed only for an `alias=` now (every aliased field renders
        // `Field(alias=…)`); `where`-constraints moved to `model_validator`. So an
        // aliased model (e.g. a pagination envelope) imports `Field`, an
        // unconstrained-and-unaliased one imports neither.
        if needs_config_dict {
            pydantic.push("Field");
        }
        if needs_bytes {
            pydantic.push("PlainSerializer");
        }
        if needs_money {
            pydantic.push("field_validator");
        }
        if needs_validator {
            pydantic.push("model_validator");
        }
        if !pydantic.is_empty() {
            self.models_out
                .push_str(&format!("from pydantic import {}\n", pydantic.join(", ")));
        }
        self.models_out.push('\n');
    }

    /// Emits the shared `FileUpload` dataclass into models.py: the client-side
    /// representation of a file part in a multipart request body — the upload
    /// filename plus the contents as `bytes`. The client passes
    /// `(upload.filename, upload.content)` to httpx's `files=` mapping so the
    /// caller-supplied filename travels on the wire (parity with Go's
    /// `FileUpload{Filename, Content}` and a TS `File`/`Blob`). It is a plain
    /// dataclass, not a pydantic model — it is never JSON-(de)serialized.
    fn emit_file_upload_dataclass(&mut self) {
        ensure_blank_lines(&mut self.models_out, 2);
        self.models_out.push_str(
            "@dataclass\n\
             class FileUpload:\n    \
             \"\"\"A client-side file part for a multipart upload (filename + content bytes).\"\"\"\n\n    \
             filename: str\n    \
             content: bytes\n",
        );
    }

    /// Emits a Pydantic BaseModel class for a Phoenix struct.
    fn emit_model(&mut self, s: &StructDecl) {
        // A file-bearing struct is body-only transport: as a multipart request it
        // is exploded into per-field `File`/`Form` params, and as a binary
        // response it is streamed as raw bytes. It is never (de)serialized as a
        // pydantic model, and `UploadFile` is not importable in models.py, so we
        // emit no class for it.
        if let Some(info) = self.check_result.module.struct_info_by_name(&s.name)
            && info.is_file_bearing
        {
            return;
        }
        ensure_blank_lines(&mut self.models_out, 2);
        if let Some(ref doc) = s.doc_comment {
            self.models_out.push_str(&render_hash_comment("", doc));
        }
        self.models_out
            .push_str(&format!("class {}(BaseModel):\n", s.name));

        if let Some(info) = self.check_result.module.struct_info_by_name(&s.name) {
            if info.fields.is_empty() {
                self.models_out.push_str("    pass\n");
            } else {
                self.emit_model_config_if_aliased(info.fields.iter().map(|f| f.name.as_str()));
                for f in &info.fields {
                    let py_type = type_to_python(&f.ty);
                    let is_option = matches!(&f.ty, Type::Generic(name, _) if name == "Option");
                    let snake = to_snake_case(&f.name);
                    self.models_out
                        .push_str(&pydantic_model_field(&snake, &py_type, is_option, &f.name));
                }
                // `where`-constraints are enforced by a model_validator over the
                // full expression (`Field(...)` now carries only the alias).
                let constrained: Vec<(String, bool, &phoenix_parser::ast::Expr, String)> = info
                    .fields
                    .iter()
                    .filter_map(|f| {
                        f.constraint.as_ref().map(|c| {
                            let is_option =
                                matches!(&f.ty, Type::Generic(name, _) if name == "Option");
                            (to_snake_case(&f.name), is_option, c, f.name.clone())
                        })
                    })
                    .collect();
                emit_constraints_validator(&mut self.models_out, &s.name, &constrained);
            }
        }
    }

    /// Emits `model_config = ConfigDict(populate_by_name=True)` as the first line of
    /// a model body when any field's wire name (the schema name) diverges from its
    /// snake_case Python attribute — i.e. when the model carries `Field(alias=…)`
    /// fields. The config lets the model construct by the Python name (handlers,
    /// tests) while parsing/serializing the schema name on the wire (camelCase,
    /// matching Go/TS). No-op for an all-single-word model, so those don't churn.
    fn emit_model_config_if_aliased<'n>(&mut self, mut wire_names: impl Iterator<Item = &'n str>) {
        let needs = wire_names.any(is_aliased);
        if needs {
            self.models_out
                .push_str("    model_config = ConfigDict(populate_by_name=True)\n\n");
        }
    }

    /// Emits a Python `str` Enum for a Phoenix enum with simple variants.
    fn emit_enum(&mut self, e: &EnumDecl) {
        let all_unit = e.variants.iter().all(|v| v.fields.is_empty());
        if !all_unit {
            return; // Skip complex enums for now
        }

        ensure_blank_lines(&mut self.models_out, 2);
        if let Some(ref doc) = e.doc_comment {
            self.models_out.push_str(&render_hash_comment("", doc));
        }
        self.models_out
            .push_str(&format!("class {}(str, Enum):\n", e.name));
        for v in &e.variants {
            self.models_out.push_str(&format!(
                "    {} = \"{}\"\n",
                to_screaming_snake(&v.name),
                v.name
            ));
        }
    }

    /// Emits a derived Pydantic model for an endpoint body type.
    fn emit_derived_model(&mut self, ep: &EndpointInfo) {
        let Some(ref body) = ep.body else { return };
        // A multipart body is exploded into per-field FastAPI params rather than a
        // pydantic model — and would reference the unimportable `UploadFile` here
        // — so no `XBody` class is emitted.
        if ep.body_is_multipart {
            return;
        }
        let type_name = format!("{}Body", capitalize(&ep.name));
        self.emit_pydantic_model(&type_name, &body.fields);
    }

    /// Emits the `<Endpoint>Response` pydantic model for an endpoint with an inline
    /// response projection (`response Struct pick/omit/partial`, incl. a `List<…>`
    /// element), mirroring [`Self::emit_derived_model`] for the response side.
    fn emit_response_projection_model(&mut self, ep: &EndpointInfo) {
        let Some(ref proj) = ep.response_projection else {
            return;
        };
        let type_name = format!("{}Response", capitalize(&ep.name));
        self.emit_pydantic_model(&type_name, &proj.fields);
    }

    /// Emits a pydantic `BaseModel` named `type_name` over `fields` (the derived
    /// field set of a request body or a response projection). Deduped via
    /// `emitted_derived_types`. Honors per-field optionality (`Option<T>` or a
    /// `partial` modifier); `where`-constraints are enforced by a trailing
    /// `@model_validator` over the full expression (see `emit_constraints_validator`),
    /// not `Field(...)` kwargs.
    fn emit_pydantic_model(&mut self, type_name: &str, fields: &[DerivedField]) {
        if !self.emitted_derived_types.insert(type_name.to_string()) {
            return;
        }

        ensure_blank_lines(&mut self.models_out, 2);
        self.models_out
            .push_str(&format!("class {}(BaseModel):\n", type_name));

        if fields.is_empty() {
            self.models_out.push_str("    pass\n");
        } else {
            self.emit_model_config_if_aliased(fields.iter().map(|f| f.name.as_str()));
            for f in fields {
                let py_type = type_to_python(&f.ty);
                // A field can be optional either because the source type is
                // `Option<T>` or because a `partial` modifier marked it optional.
                // Avoid emitting `T | None | None` when both apply. Test the
                // source type structurally rather than sniffing the rendered
                // string: a container like `List<Option<T>>` renders as
                // `list[T | None]`, which is *not* itself optional and still
                // needs the outer `| None` when `partial` marks the field.
                let already_optional = matches!(&f.ty, Type::Generic(name, _) if name == "Option");
                let optional_type = if f.optional && !already_optional {
                    format!("{py_type} | None")
                } else {
                    py_type.clone()
                };
                let field_ty = if f.optional { &optional_type } else { &py_type };
                let snake = to_snake_case(&f.name);
                self.models_out
                    .push_str(&pydantic_model_field(&snake, field_ty, f.optional, &f.name));
            }
            // Enforce `where`-constraints over the full expression (see emit_model).
            // A field is checked-when-present if it's `partial`-optional or `Option<T>`.
            let constrained: Vec<(String, bool, &phoenix_parser::ast::Expr, String)> = fields
                .iter()
                .filter_map(|f| {
                    f.constraint.as_ref().map(|c| {
                        let is_option = f.optional
                            || matches!(&f.ty, Type::Generic(name, _) if name == "Option");
                        (to_snake_case(&f.name), is_option, c, f.name.clone())
                    })
                })
                .collect();
            emit_constraints_validator(&mut self.models_out, type_name, &constrained);
        }
    }

    /// Emits the `<Endpoint>Result` typed envelope model for an endpoint that
    /// declares response headers: the response body plus one typed field per
    /// response header (snake_case local name). Endpoints without response
    /// headers emit nothing here — they keep returning the bare response type.
    fn emit_result_envelope(&mut self, ep: &EndpointInfo) {
        if ep.response_headers.is_empty() {
            return;
        }
        let type_name = format!("{}Result", capitalize(&ep.name));
        if !self.emitted_derived_types.insert(type_name.clone()) {
            return;
        }

        let body_type = ep
            .response
            .as_ref()
            .map(type_to_python)
            .unwrap_or_else(|| "None".to_string());

        ensure_blank_lines(&mut self.models_out, 2);
        self.models_out
            .push_str(&format!("class {type_name}(BaseModel):\n"));
        self.models_out
            .push_str(&format!("    body: {body_type}\n"));
        for hp in &ep.response_headers {
            let snake = to_snake_case(&hp.name);
            let py_type = type_to_python(&hp.ty);
            // Response-header optionality is `Option<T>` ONLY. A `= default` on a
            // response header is meaningless (sema ignores it; the handler sets
            // the value), so a default must not flip the field to `T | None`.
            let is_optional = matches!(&hp.ty, Type::Generic(name, _) if name == "Option");
            if is_optional {
                self.models_out
                    .push_str(&format!("    {snake}: {py_type} = None\n"));
            } else {
                self.models_out
                    .push_str(&format!("    {snake}: {py_type}\n"));
            }
        }
    }

    /// Emits the `<Endpoint>Page` pagination envelope model for an endpoint that
    /// declares a `pagination { }` block. The envelope wraps the bare `List<T>`
    /// response in a typed page with mode-specific metadata:
    /// - **offset** → `class ListPostsPage(BaseModel): items: list[Post];
    ///   total_count: int`
    /// - **cursor** → `class ListPostsPage(BaseModel): items: list[Post];
    ///   next_cursor: str | None = None`
    ///
    /// The page envelope IS serialized to JSON (it is the paginated response body),
    /// so its wire keys must match Go's `json:"totalCount"`/`"nextCursor"` and TS's
    /// `totalCount`/`nextCursor`. The metadata fields carry `Field(alias="…")` (with
    /// `populate_by_name` so the handler still builds them by the snake name);
    /// `items` is single-word and needs none.
    ///
    /// Endpoints without pagination emit nothing here and keep returning their
    /// bare response type. Pagination and response headers are mutually exclusive
    /// (sema rejects the combination), so this never collides with the
    /// `<Endpoint>Result` envelope.
    fn emit_page_model(&mut self, ep: &EndpointInfo) {
        let Some(ref pag) = ep.pagination else {
            return;
        };
        let type_name = page_type_name(ep);
        if !self.emitted_derived_types.insert(type_name.clone()) {
            return;
        }
        let item_type = type_to_python(&pag.item_type);

        ensure_blank_lines(&mut self.models_out, 2);
        self.models_out
            .push_str(&format!("class {type_name}(BaseModel):\n"));
        // Wire names (camelCase) the model aliases to; `items` matches its snake
        // form, the metadata field does not.
        let meta_wire = match pag.mode {
            PaginationMode::Offset => "totalCount",
            PaginationMode::Cursor => "nextCursor",
        };
        self.emit_model_config_if_aliased(["items", meta_wire].into_iter());
        self.models_out
            .push_str(&format!("    items: list[{item_type}]\n"));
        match pag.mode {
            PaginationMode::Offset => {
                self.models_out.push_str(&pydantic_model_field(
                    "total_count",
                    "int",
                    false,
                    "totalCount",
                ));
            }
            // `next_cursor` is null/absent on the last page — render it optional
            // with a `None` default, matching how an optional envelope field is
            // emitted elsewhere.
            PaginationMode::Cursor => {
                self.models_out.push_str(&pydantic_model_field(
                    "next_cursor",
                    "str | None",
                    true,
                    "nextCursor",
                ));
            }
        }
    }

    /// Emits the `<Endpoint>Response` multi-status envelope model for an endpoint
    /// that declares a `response { }` block (`response_statuses` non-empty). The
    /// handler returns, and the client observes, this envelope instead of the bare
    /// body:
    /// - `status: int` — the actual HTTP status (handler sets it, server writes it,
    ///   client reads it).
    /// - `body: T | None = None` — the shared body type as an Option, present only
    ///   when the block declares at least one typed status (`ep.response` is
    ///   `Some`). An all-typeless block (e.g. `response { 202  204 }`) has no `T`,
    ///   so the envelope is just `{ status: int }` with no `body` field.
    ///
    /// Field names are snake_case attributes that ARE the wire names — the Python
    /// generator emits NO `Field(alias=...)` on any model, matching every other
    /// Python model. (`status`/`body` are single words, so no alias is needed.)
    ///
    /// This mirrors the response-header `<Endpoint>Result` / pagination
    /// `<Endpoint>Page` envelope machinery; all three are mutually exclusive (sema
    /// rejects the combinations). Endpoints WITHOUT a `response { }` block emit
    /// nothing and keep returning the bare body unchanged.
    fn emit_multi_status_envelope(&mut self, ep: &EndpointInfo) {
        if ep.response_statuses.is_empty() {
            return;
        }
        let type_name = multi_status_type_name(ep);
        if !self.emitted_derived_types.insert(type_name.clone()) {
            return;
        }

        ensure_blank_lines(&mut self.models_out, 2);
        self.models_out
            .push_str(&format!("class {type_name}(BaseModel):\n"));
        self.models_out.push_str("    status: int\n");
        if let Some(body_type) = ep.response.as_ref().map(type_to_python) {
            self.models_out
                .push_str(&format!("    body: {body_type} | None = None\n"));
        }
    }

    // ── client.py emission ──────────────────────────────────────────

    /// Emits imports for the client file.
    fn emit_client_imports(&mut self, needs_datetime: bool, needs_uuid: bool, needs_decimal: bool) {
        // `Any` is referenced by the `params: dict[str, Any]` dict that
        // query-bearing endpoints emit, and by the `data: dict[str, Any]` form
        // dict that multipart endpoints emit; importing it otherwise trips ruff
        // F401.
        let needs_any = self
            .check_result
            .endpoints
            .iter()
            .any(|ep| !ep.query_params.is_empty() || ep.body_is_multipart);
        // A bare/collection `Bytes` response leaf (reached without a `Named`
        // struct, whose model runs the alias on construction) is decoded inline by
        // `py_decode_expr` via `base64.b64decode`, so the client needs `base64`. A
        // struct-wrapped `Bytes` needs nothing here — pydantic runs the alias on
        // `Model(**...)`. Mirrors the `py_decode_expr` `Type::Bytes` arm: same gate
        // (plain response path — not multi-status/pagination/binary) and the same
        // `type_uses_bytes` (recurses generics, stops at `Named`).
        //
        // Multi-status and pagination are excluded NOT because they can't carry
        // `Bytes` but because they never reach `py_decode_expr`: both decode their
        // body through `model_validate` on a generated envelope/page model
        // (`<Endpoint>Response` / `<Endpoint>Page`), so any `Bytes` inside runs the
        // pydantic alias — never the inline `base64.b64decode` that needs the
        // import. (A bare-scalar multi-status body isn't expressible: `model_validate`
        // requires a model type, the same constraint that applies to `Money`/`Decimal`.)
        let needs_base64 = self.check_result.endpoints.iter().any(|ep| {
            ep.response_statuses.is_empty()
                && ep.pagination.is_none()
                && !ep.response_is_binary
                && ep.response.as_ref().is_some_and(type_uses_bytes)
        });
        // stdlib group: a straight `import` (isort) sorts before the `from … import`
        // block, then a blank line, then the third-party `httpx`. (from-imports:
        // datetime < decimal < typing < uuid.)
        let mut stdlib: Vec<&str> = Vec::new();
        if needs_base64 {
            stdlib.push("import base64");
        }
        if needs_datetime {
            stdlib.push("from datetime import datetime");
        }
        if needs_decimal {
            stdlib.push("from decimal import Decimal");
        }
        if needs_any {
            stdlib.push("from typing import Any");
        }
        if needs_uuid {
            stdlib.push("from uuid import UUID");
        }
        if !stdlib.is_empty() {
            self.client_out.push_str(&stdlib.join("\n"));
            self.client_out.push_str("\n\n");
        }
        self.client_out.push_str("import httpx\n");

        let mut imports = BTreeSet::new();
        // A multipart client method takes its file fields as the shared
        // `FileUpload` dataclass (emitted into models.py), so import it.
        if self
            .check_result
            .endpoints
            .iter()
            .any(|ep| ep.body_is_multipart)
        {
            imports.insert("FileUpload".to_string());
        }
        for ep in &self.check_result.endpoints {
            // A multipart body is sent as `files=`/`data=` (no `XBody` model). A
            // binary download returns `bytes` (no response struct constructed).
            if ep.body.is_some() && !ep.body_is_multipart {
                imports.insert(format!("{}Body", capitalize(&ep.name)));
            }
            // A paginated endpoint parses the body into `<Endpoint>Page` (the bare
            // item type is referenced only inside that model in models.py), so
            // import the page model and NOT the item type — importing the unused
            // item type here would trip ruff F401.
            // A multi-status endpoint returns the `<Endpoint>Response` envelope
            // AND — unlike pagination — parses the shared body directly
            // (`T.model_validate(response.json())`) before wrapping it, so the
            // client references BOTH names: import the envelope and the body
            // type. (An all-typeless block has no `T` and imports only the
            // envelope.)
            if !ep.response_statuses.is_empty() {
                imports.insert(multi_status_type_name(ep));
                if let Some(ref resp) = ep.response {
                    collect_python_imports(resp, &mut imports);
                }
            } else if ep.pagination.is_some() {
                imports.insert(page_type_name(ep));
            } else if let Some(ref resp) = ep.response
                && !ep.response_is_binary
            {
                collect_python_imports(resp, &mut imports);
            }
            // Endpoints with response headers return the typed envelope, which
            // lives in models.py and must be imported here.
            if !ep.response_headers.is_empty() {
                imports.insert(format!("{}Result", capitalize(&ep.name)));
            }
            // Enum query/request-header param types are named in the method
            // signature; enum response-header types are constructed on read
            // (`Color(raw)`). `collect_python_imports` adds only user models.
            for q in &ep.query_params {
                collect_python_imports(&q.ty, &mut imports);
            }
            for h in ep.headers.iter().chain(ep.response_headers.iter()) {
                collect_python_imports(&h.ty, &mut imports);
            }
        }
        if !imports.is_empty() {
            let joined: Vec<_> = imports.into_iter().collect();
            self.client_out.push('\n');
            self.client_out
                .push_str(&format_from_import(".models", &joined));
        }
    }

    /// Emits a single async client method for an endpoint.
    fn emit_client_method(&mut self, ep: &EndpointInfo) {
        let method = ep.method.as_lower_str();
        let fn_name = to_snake_case(&ep.name);
        // The bare response body type; the method's return type becomes the typed
        // `<Endpoint>Result` envelope when the endpoint declares response headers,
        // or `bytes` for a binary download.
        let body_type = if ep.response_is_binary {
            "bytes".to_string()
        } else {
            ep.response
                .as_ref()
                .map(type_to_python)
                .unwrap_or_else(|| "None".to_string())
        };
        let response_type = if !ep.response_statuses.is_empty() {
            multi_status_type_name(ep)
        } else if ep.pagination.is_some() {
            page_type_name(ep)
        } else if ep.response_headers.is_empty() {
            body_type.clone()
        } else {
            format!("{}Result", capitalize(&ep.name))
        };

        // Build parameter list
        let mut params = vec!["self".to_string()];
        for pp in &ep.path_params {
            params.push(format!("{}: str", to_snake_case(pp)));
        }
        if ep.body_is_multipart {
            // Multipart: each body field is a client param. `File` fields take a
            // `FileUpload` (filename + content bytes); optional files are
            // `FileUpload | None`. Scalars use their normal Python type. Required
            // params sort before optional ones (which carry `= None`).
            let body = ep
                .body
                .as_ref()
                .expect("multipart body has resolved fields");
            let mut required = Vec::new();
            let mut optional = Vec::new();
            for f in &body.fields {
                let snake = to_snake_case(&f.name);
                let is_file = matches!(&f.ty, Type::File);
                let is_opt_file = matches!(
                    &f.ty,
                    Type::Generic(name, args)
                        if name == "Option" && matches!(args.first(), Some(Type::File))
                );
                let already_optional = matches!(&f.ty, Type::Generic(name, _) if name == "Option");
                // A `File` field relaxed by `partial` (`f.optional`) is an
                // optional upload, exactly like `Option<File>`.
                if is_file && !f.optional {
                    required.push(format!("{snake}: FileUpload"));
                } else if is_opt_file || is_file {
                    optional.push(format!("{snake}: FileUpload | None = None"));
                } else if f.optional || already_optional {
                    let py_type = type_to_python(&f.ty);
                    let ty = if f.optional && !already_optional {
                        format!("{py_type} | None")
                    } else {
                        py_type
                    };
                    optional.push(format!("{snake}: {ty} = None"));
                } else {
                    required.push(format!("{snake}: {}", type_to_python(&f.ty)));
                }
            }
            params.extend(required);
            params.extend(optional);
        } else if ep.body.is_some() {
            let body_type = format!("{}Body", capitalize(&ep.name));
            params.push(format!("body: {body_type}"));
        }
        // Query params and request headers are keyword-only.
        if !ep.query_params.is_empty() || !ep.headers.is_empty() {
            params.push("*".to_string());
            for qp in &ep.query_params {
                let py_type = type_to_python(&qp.ty);
                let is_optional =
                    qp.has_default || matches!(&qp.ty, Type::Generic(name, _) if name == "Option");
                if is_optional {
                    let default = qp
                        .default_value
                        .as_ref()
                        .map(|d| py_default_for(d, &py_type))
                        .unwrap_or_else(|| "None".to_string());
                    params.push(format!(
                        "{}: {} = {}",
                        to_snake_case(&qp.name),
                        py_type,
                        default
                    ));
                } else {
                    params.push(format!("{}: {}", to_snake_case(&qp.name), py_type));
                }
            }
            for hp in &ep.headers {
                let py_type = type_to_python(&hp.ty);
                let is_optional =
                    hp.has_default || matches!(&hp.ty, Type::Generic(name, _) if name == "Option");
                if is_optional {
                    let default = hp
                        .default_value
                        .as_ref()
                        .map(|d| py_default_for(d, &py_type))
                        .unwrap_or_else(|| "None".to_string());
                    params.push(format!(
                        "{}: {} = {}",
                        to_snake_case(&hp.name),
                        py_type,
                        default
                    ));
                } else {
                    params.push(format!("{}: {}", to_snake_case(&hp.name), py_type));
                }
            }
        }

        ensure_blank_lines(&mut self.client_out, 1);
        if let Some(ref doc) = ep.doc_comment {
            self.client_out.push_str(&render_hash_comment("    ", doc));
        }

        let sig = format_def_signature(
            "    ",
            "async def",
            &fn_name,
            &params,
            &format!(" -> {response_type}:"),
        );
        self.client_out.push_str(&sig);

        // Build URL
        let url = build_python_url(&ep.path);
        let has_query = !ep.query_params.is_empty();
        let has_headers = !ep.headers.is_empty();

        // Query params dict
        if has_query {
            self.client_out
                .push_str("        params: dict[str, Any] = {}\n");
            for qp in &ep.query_params {
                let snake = to_snake_case(&qp.name);
                // A list-valued query param: httpx sends a list value as repeated
                // keys. Encode each element to its wire string. The list is required
                // (no `Option<List>`), so it is set unconditionally.
                if let Type::Generic(lname, largs) = &qp.ty
                    && lname == "List"
                    && largs.len() == 1
                {
                    let value = py_list_query_value(&largs[0], &snake);
                    self.client_out
                        .push_str(&format!("        params[\"{}\"] = {value}\n", qp.name));
                    continue;
                }
                // A `DateTime` goes on the wire as RFC 3339; httpx would otherwise
                // `str()` it into Python's space-separated default layout, which the
                // server's ISO parse rejects. `.isoformat()` emits the `T`-form.
                let value = match unwrap_option(&qp.ty) {
                    Type::DateTime => format!("{snake}.isoformat()"),
                    // httpx would stringify a `UUID`/`Decimal` to its canonical form
                    // anyway, but make it explicit so the wire value is unambiguous.
                    Type::Uuid | Type::Decimal => format!("str({snake})"),
                    // An enum is a `(str, Enum)` — send its `.value` so the wire
                    // string is the bare variant (`str(enum)` would emit
                    // `Color.Red`).
                    Type::Named(_) => format!("{snake}.value"),
                    _ => snake.clone(),
                };
                self.client_out.push_str(&format!(
                    "        if {snake} is not None:\n            params[\"{}\"] = {value}\n",
                    qp.name
                ));
            }
        }

        // Request headers dict. httpx wants `str` header values, so non-string
        // types are stringified; optional headers are only set when present. The
        // wire name is the exact HTTP header string (never recomputed here).
        if has_headers {
            self.client_out
                .push_str("        headers: dict[str, str] = {}\n");
            for hp in &ep.headers {
                let snake = to_snake_case(&hp.name);
                // A list-valued request header: comma-join the encoded elements
                // into a single header value (the server splits on `,`).
                if let Type::Generic(lname, largs) = &hp.ty
                    && lname == "List"
                    && largs.len() == 1
                {
                    let joined = py_list_header_value(&largs[0], &snake);
                    self.client_out.push_str(&format!(
                        "        headers[\"{}\"] = {joined}\n",
                        hp.wire_name
                    ));
                    continue;
                }
                // Only an `Option<T>` header can actually be `None` at runtime, so
                // only those are guarded. A header with a literal default is a
                // non-None value (it defaults to that literal) and is sent
                // unconditionally — guarding it would be an always-true check.
                let is_nullable = matches!(&hp.ty, Type::Generic(name, _) if name == "Option");
                let value = match &unwrap_option(&hp.ty) {
                    // `str` headers pass through untouched.
                    Type::String => snake.clone(),
                    // `bool` must serialize as lowercase `true`/`false` to match
                    // every other path (Go `strconv.FormatBool`, TS `String(bool)`,
                    // and this generator's own response-header set/read which use
                    // lowercase). Python's `str(True)` would emit `"True"`, which
                    // the TS server's `=== "true"` coercion rejects. No surrounding
                    // parens: the value lands on the RHS of a `headers[...] = `
                    // assignment, where black strips redundant parentheses.
                    Type::Bool => format!("\"true\" if {snake} else \"false\""),
                    // RFC 3339 on the wire; `str(datetime)` would use Python's
                    // space-separated layout, which the peer's ISO parse rejects.
                    Type::DateTime => format!("{snake}.isoformat()"),
                    // An enum is a `(str, Enum)` — send its `.value` (the bare
                    // variant), not `str(enum)` which emits `Color.Red`.
                    Type::Named(_) => format!("{snake}.value"),
                    _ => format!("str({snake})"),
                };
                if is_nullable {
                    // Only send the header when the caller provided a value.
                    self.client_out.push_str(&format!(
                        "        if {snake} is not None:\n            headers[\"{}\"] = {value}\n",
                        hp.wire_name
                    ));
                } else {
                    self.client_out.push_str(&format!(
                        "        headers[\"{}\"] = {value}\n",
                        hp.wire_name
                    ));
                }
            }
        }

        // Multipart: build the `files` (binary) and `data` (scalar) dicts httpx
        // sends as `multipart/form-data`. File fields keyed by the wire name carry
        // an `(filename, content)` tuple — httpx's file shape that pins the
        // upload filename; optional files/scalars are only added when provided.
        // The keys are the schema's original field names so the server's
        // `File`/`Form` params (aliased when snake-cased) match.
        if ep.body_is_multipart {
            let body = ep
                .body
                .as_ref()
                .expect("multipart body has resolved fields");
            self.client_out
                .push_str("        files: dict[str, tuple[str, bytes]] = {}\n");
            self.client_out
                .push_str("        data: dict[str, Any] = {}\n");
            for f in &body.fields {
                let snake = to_snake_case(&f.name);
                let is_file = matches!(&f.ty, Type::File);
                let is_opt_file = matches!(
                    &f.ty,
                    Type::Generic(name, args)
                        if name == "Option" && matches!(args.first(), Some(Type::File))
                );
                let is_optional =
                    f.optional || matches!(&f.ty, Type::Generic(name, _) if name == "Option");
                let (target, value) = if is_file || is_opt_file {
                    ("files", format!("({snake}.filename, {snake}.content)"))
                } else {
                    // Multipart form values are text on the wire. A bool left as
                    // a Python value would be rendered by httpx's form encoder as
                    // "True"/"False" (Python's default bool formatting); coerce it
                    // to canonical lowercase "true"/"false" instead so it matches
                    // every target's server parse uniformly — notably the
                    // generated TS server, which compares `=== "true"` (strict
                    // lowercase) and would read "True" as false. (Other scalars
                    // need no special-casing — httpx stringifies int/float as-is.)
                    let inner = match &f.ty {
                        Type::Generic(name, args) if name == "Option" && !args.is_empty() => {
                            &args[0]
                        }
                        other => other,
                    };
                    // No surrounding parens: the value lands on the RHS of a
                    // `data[...] = ` assignment, where black strips redundant ones.
                    let value = if matches!(inner, Type::Bool) {
                        format!("\"true\" if {snake} else \"false\"")
                    } else {
                        snake.clone()
                    };
                    ("data", value)
                };
                if is_optional {
                    self.client_out.push_str(&format!(
                        "        if {snake} is not None:\n            {target}[\"{}\"] = {value}\n",
                        f.name
                    ));
                } else {
                    self.client_out
                        .push_str(&format!("        {target}[\"{}\"] = {value}\n", f.name));
                }
            }
        }

        // HTTP call
        let mut call_args = vec![format!("f\"{{self.base_url}}{url}\"")];
        if ep.body_is_multipart {
            call_args.push("files=files".to_string());
            call_args.push("data=data".to_string());
        } else if let Some(ref body) = ep.body {
            // `model_dump()` returns Python objects (a `datetime` stays a
            // `datetime`), which httpx's `json=` (plain `json.dumps`) cannot
            // serialize. `mode="json"` makes pydantic render JSON-safe values
            // (e.g. `datetime` → RFC 3339 string). Only needed when the body
            // carries a `DateTime`; other types are already JSON-safe under the
            // default dump, so keep that form to avoid churning every snapshot.
            // The check is transitive (`*_deep`): a `DateTime`/`Uuid` reached only
            // through a nested struct field still leaves a raw `datetime`/`UUID`
            // object in the default dump, so it must force JSON mode too. `UUID`
            // has the identical problem as `datetime` — `json.dumps` rejects it.
            // `Bytes` is intentionally NOT in this set: its alias's `PlainSerializer`
            // runs with the default `when_used="always"`, so a `Bytes` field is
            // already rendered to a base64 string under the plain `model_dump()` —
            // unlike `datetime`/`UUID`/`Decimal`, it never leaves a non-JSON-safe
            // value that would force JSON mode.
            let needs_json_dump = body.fields.iter().any(|f| {
                type_uses_datetime_deep(&f.ty, self.check_result, &mut BTreeSet::new())
                    || type_uses_uuid_deep(&f.ty, self.check_result, &mut BTreeSet::new())
                    || type_uses_decimal_deep(&f.ty, self.check_result, &mut BTreeSet::new())
            });
            // `by_alias=True` serializes the schema (camelCase) wire names so the
            // body matches what the Go/TS targets send/expect — and it recurses, so
            // a nested model's aliased fields are covered too. It is a no-op when
            // nothing is aliased (the alias equals the field name), so it is emitted
            // unconditionally rather than gated on a transitive-alias scan.
            let dump = if needs_json_dump {
                "body.model_dump(mode=\"json\", by_alias=True)"
            } else {
                "body.model_dump(by_alias=True)"
            };
            call_args.push(format!("json={dump}"));
        }
        if has_query {
            call_args.push("params=params".to_string());
        }
        if has_headers {
            call_args.push("headers=headers".to_string());
        }
        let call = format_call(
            "        ",
            &format!("response = await self.client.{method}"),
            &call_args,
            "",
        );
        self.client_out.push_str(&call);
        self.client_out
            .push_str("        response.raise_for_status()\n");

        // Return
        if response_type == "None" {
            return;
        }

        // Multi-status: build the `<Endpoint>Response` envelope from the HTTP
        // response — the handler-chosen status comes from `response.status_code`;
        // the optional shared body is parsed into `T | None` only when the
        // response actually carries one (a typeless status like 204 sends no
        // body). An all-typeless block (no `T`) has no `body` field, so just
        // record the status.
        if !ep.response_statuses.is_empty() {
            if let Some(ref resp) = ep.response {
                let body_ty = type_to_python(resp);
                // The local is named `response_body` (not `body`) so it never
                // shadows the request-body parameter (also `body`), which would
                // trip mypy with conflicting types; the explicit `T | None`
                // annotation keeps mypy happy with the conditional assignment.
                // The empty-body guard is on CONTENT only — never a status-code
                // special case — because ANY typeless status (202, 204, ...)
                // sends an empty body (design decision 5; mirrors the Go
                // ContentLength/EOF guard and the TS non-empty `response.text()`
                // check). A statement (not a conditional expression) so black
                // never re-wraps it based on the body type's name length.
                self.client_out.push_str(&format!(
                    "        response_body: {body_ty} | None = None\n        if response.content:\n            response_body = {body_ty}.model_validate(response.json())\n"
                ));
                // Through `format_call` so a long `<Endpoint>Response` name wraps
                // the way black does (`Response(\n    status=…, body=…\n)`) instead
                // of overflowing the line length (black reformat + ruff E501).
                let ret = format_call(
                    "        ",
                    &format!("return {response_type}"),
                    &[
                        "status=response.status_code".to_string(),
                        "body=response_body".to_string(),
                    ],
                    "",
                );
                self.client_out.push_str(&ret);
            } else {
                let ret = format_call(
                    "        ",
                    &format!("return {response_type}"),
                    &["status=response.status_code".to_string()],
                    "",
                );
                self.client_out.push_str(&ret);
            }
            return;
        }

        // Binary download: return the raw response bytes (no JSON parsing).
        if ep.response_is_binary {
            self.client_out
                .push_str("        return response.content\n");
            return;
        }

        // A paginated endpoint parses the whole JSON body into the typed
        // `<Endpoint>Page` envelope (items + mode metadata). `model_validate`
        // constructs the nested `items: list[T]` from the wire array.
        if ep.pagination.is_some() {
            self.client_out.push_str(&format!(
                "        return {response_type}.model_validate(response.json())\n"
            ));
            return;
        }

        // Decode the JSON body into the bare response type. `py_decode_expr`
        // dispatches on the type: a struct via `Model(**...)` (pydantic coerces
        // nested fields), a scalar via its constructor (`int`/`str`/...,
        // `datetime.fromisoformat` for `DateTime`), and collections element-wise.
        let body_expr = ep
            .response
            .as_ref()
            .map(|ty| py_decode_expr(ty, "response.json()"))
            .unwrap_or_else(|| "response.json()".to_string());

        if ep.response_headers.is_empty() {
            self.client_out
                .push_str(&format!("        return {body_expr}\n"));
            return;
        }

        // Typed envelope: read each response header off the httpx response and
        // coerce to the declared type. Absent optional headers stay `None`;
        // required headers are coerced directly (the raw `.get` is `str | None`,
        // so the `<wire>` local carries that union and the coercion narrows it).
        let mut result_args = vec![format!("body={body_expr}")];
        for hp in &ep.response_headers {
            let snake = to_snake_case(&hp.name);
            // Response-header optionality is `Option<T>` ONLY (a `= default` is
            // meaningless for a response header), matching the envelope field.
            let is_optional = matches!(&hp.ty, Type::Generic(name, _) if name == "Option");
            let inner_ty = unwrap_option(&hp.ty);
            self.client_out.push_str(&format!(
                "        {snake}_raw = response.headers.get(\"{}\")\n",
                hp.wire_name
            ));
            // `str` headers pass through untouched; other scalar types are parsed
            // from the wire string. `int`/`float`/`bool` get a converter.
            self.client_out
                .push_str(&header_read_coercion(&inner_ty, &snake, is_optional));
            result_args.push(format!("{snake}={snake}"));
        }
        let ret = format_call(
            "        ",
            &format!("return {response_type}"),
            &result_args,
            "",
        );
        self.client_out.push_str(&ret);
    }

    // ── handlers.py emission ────────────────────────────────────────

    /// Emits imports for the handlers file.
    fn emit_handler_imports(
        &mut self,
        needs_datetime: bool,
        needs_uuid: bool,
        needs_decimal: bool,
    ) {
        // isort order within the stdlib group: datetime < decimal < typing < uuid.
        if needs_datetime {
            self.handlers_out
                .push_str("from datetime import datetime\n");
        }
        if needs_decimal {
            self.handlers_out.push_str("from decimal import Decimal\n");
        }
        self.handlers_out.push_str("from typing import Protocol\n");
        if needs_uuid {
            self.handlers_out.push_str("from uuid import UUID\n");
        }

        // A multipart handler receives `UploadFile` params, imported from fastapi.
        let needs_upload_file = self
            .check_result
            .endpoints
            .iter()
            .any(|ep| ep.body_is_multipart);
        if needs_upload_file {
            self.handlers_out
                .push_str("\nfrom fastapi import UploadFile\n");
        }

        let mut imports = BTreeSet::new();
        for ep in &self.check_result.endpoints {
            // A multipart body is delivered as per-field params (no `XBody`
            // model). A binary download returns `bytes` (no response struct).
            if ep.body.is_some() && !ep.body_is_multipart {
                imports.insert(format!("{}Body", capitalize(&ep.name)));
            }
            // A paginated handler returns `<Endpoint>Page` (the item type is only
            // named inside that model), so import the page model, not the item
            // type — an unused item-type import would trip ruff F401.
            // A multi-status handler returns the `<Endpoint>Response` envelope; the
            // shared body type is named only inside that model, so import the
            // envelope and not the item type (an unused import would trip F401).
            if !ep.response_statuses.is_empty() {
                imports.insert(multi_status_type_name(ep));
            } else if ep.pagination.is_some() {
                imports.insert(page_type_name(ep));
            } else if !ep.response_headers.is_empty() {
                // Response-header endpoint: the handler returns the `<Endpoint>Result`
                // envelope (which bundles the body type internally — referenced in
                // models.py, not here), so import only the envelope. Importing the
                // bare body type too would be unused (ruff F401) for a projected
                // `<Endpoint>Response`, which no other endpoint shares.
                imports.insert(format!("{}Result", capitalize(&ep.name)));
            } else if let Some(ref resp) = ep.response
                && !ep.response_is_binary
            {
                collect_python_imports(resp, &mut imports);
            }
            // Enum query/request-header param types are named in the handler
            // signature (response-header enums travel inside the imported envelope).
            for q in &ep.query_params {
                collect_python_imports(&q.ty, &mut imports);
            }
            for h in &ep.headers {
                collect_python_imports(&h.ty, &mut imports);
            }
        }
        if !imports.is_empty() {
            let joined: Vec<_> = imports.into_iter().collect();
            self.handlers_out.push('\n');
            self.handlers_out
                .push_str(&format_from_import(".models", &joined));
        }
    }

    /// Emits a single handler method signature.
    fn emit_handler_method(&mut self, ep: &EndpointInfo) {
        let fn_name = to_snake_case(&ep.name);
        // A binary download handler returns the raw `bytes`; the server wraps
        // them in a FastAPI `Response`.
        let response_type = if !ep.response_statuses.is_empty() {
            multi_status_type_name(ep)
        } else if ep.response_is_binary {
            "bytes".to_string()
        } else if ep.pagination.is_some() {
            page_type_name(ep)
        } else if ep.response_headers.is_empty() {
            ep.response
                .as_ref()
                .map(type_to_python)
                .unwrap_or_else(|| "None".to_string())
        } else {
            format!("{}Result", capitalize(&ep.name))
        };

        let mut params = vec!["self".to_string()];
        for pp in &ep.path_params {
            params.push(format!("{}: str", to_snake_case(pp)));
        }
        if ep.body_is_multipart {
            // Multipart: each body field becomes a handler param. `File` fields
            // are typed `UploadFile` (`UploadFile | None` when optional); scalars
            // use their normal Python type. Required fields sort before optional
            // ones (which carry `= None`) so no non-default follows a default.
            let body = ep
                .body
                .as_ref()
                .expect("multipart body has resolved fields");
            let mut required = Vec::new();
            let mut optional = Vec::new();
            for f in &body.fields {
                let snake = to_snake_case(&f.name);
                let is_file = matches!(&f.ty, Type::File);
                let is_opt_file = matches!(
                    &f.ty,
                    Type::Generic(name, args)
                        if name == "Option" && matches!(args.first(), Some(Type::File))
                );
                let already_optional = matches!(&f.ty, Type::Generic(name, _) if name == "Option");
                // A `File` field relaxed by `partial` (`f.optional`) is an
                // optional upload, exactly like `Option<File>`.
                if is_file && !f.optional {
                    required.push(format!("{snake}: UploadFile"));
                } else if is_opt_file || is_file {
                    optional.push(format!("{snake}: UploadFile | None = None"));
                } else if f.optional || already_optional {
                    let py_type = type_to_python(&f.ty);
                    let ty = if f.optional && !already_optional {
                        format!("{py_type} | None")
                    } else {
                        py_type
                    };
                    optional.push(format!("{snake}: {ty} = None"));
                } else {
                    required.push(format!("{snake}: {}", type_to_python(&f.ty)));
                }
            }
            params.extend(required);
            params.extend(optional);
        } else if ep.body.is_some() {
            let body_type = format!("{}Body", capitalize(&ep.name));
            params.push(format!("body: {body_type}"));
        }
        if !ep.query_params.is_empty() || !ep.headers.is_empty() {
            params.push("*".to_string());
            for qp in &ep.query_params {
                let py_type = type_to_python(&qp.ty);
                let is_option = matches!(&qp.ty, Type::Generic(name, _) if name == "Option");
                if is_option {
                    params.push(format!("{}: {} = None", to_snake_case(&qp.name), py_type));
                } else {
                    params.push(format!("{}: {}", to_snake_case(&qp.name), py_type));
                }
            }
            for hp in &ep.headers {
                let py_type = type_to_python(&hp.ty);
                // Mirrors query params: the handler only sees `= None` for an
                // `Option<T>` header. A header with a literal default is resolved
                // by the server (`Header(<default>, ...)`) before the handler is
                // called, so it arrives as a plain required value here.
                let is_option = matches!(&hp.ty, Type::Generic(name, _) if name == "Option");
                if is_option {
                    params.push(format!("{}: {} = None", to_snake_case(&hp.name), py_type));
                } else {
                    params.push(format!("{}: {}", to_snake_case(&hp.name), py_type));
                }
            }
        }

        ensure_blank_lines(&mut self.handlers_out, 1);
        if let Some(ref doc) = ep.doc_comment {
            self.handlers_out
                .push_str(&render_hash_comment("    ", doc));
        }
        let sig = format_def_signature(
            "    ",
            "async def",
            &fn_name,
            &params,
            &format!(" -> {response_type}: ..."),
        );
        self.handlers_out.push_str(&sig);
    }

    // ── server.py emission ──────────────────────────────────────────

    /// Emits imports for the server file.
    fn emit_server_imports(&mut self, needs_datetime: bool, needs_uuid: bool, needs_decimal: bool) {
        // Build the `fastapi` import list from what the routes actually use, so
        // ruff F401 never fires on an unused name. `APIRouter` is always needed;
        // `HTTPException` only when some endpoint maps errors; `Query` only when
        // some query param needs an `alias=` (its wire name differs from the
        // snake_case Python parameter — see `emit_server_route`).
        // `HTTPException` is raised both by the error-mapping `except` blocks and by
        // the inline multipart `where`-constraint checks (a constraint on an exploded
        // Form field has no model_validator to live in, so the route raises 422
        // directly). Either pulls in the import.
        let needs_http_exception = self.check_result.endpoints.iter().any(|ep| {
            !ep.errors.is_empty()
                || (ep.body_is_multipart
                    && ep
                        .body
                        .as_ref()
                        .is_some_and(|b| b.fields.iter().any(|f| f.constraint.is_some())))
        });
        let needs_query = self.check_result.endpoints.iter().any(|ep| {
            ep.query_params.iter().any(|qp| {
                // An aliased param OR any list-valued param (always bound via
                // `Query(default_factory=list)`) pulls in `Query`.
                to_snake_case(&qp.name) != qp.name
                    || matches!(&qp.ty, Type::Generic(n, _) if n == "List")
            })
        });
        // Request headers always bind via `Header(alias="<wire_name>")` — the
        // exact HTTP header name (e.g. `Idempotency-Key`) is never a valid Python
        // identifier — so any request header pulls in `Header`. Response headers
        // are set on a `Response` param, so any of those pulls in `Response`.
        let needs_header = self
            .check_result
            .endpoints
            .iter()
            .any(|ep| !ep.headers.is_empty());
        // A multipart request body explodes into per-field params. The exact
        // fastapi imports depend on which field shapes appear:
        //  - any multipart body has ≥1 `File` field → `UploadFile` (param type);
        //  - a `File` field whose snake_case name differs from the wire name binds
        //    `Annotated[UploadFile, File(alias=...)]` → `File` + `Annotated`;
        //  - any scalar field binds `Form(...)`/`Form(None)` → `Form`.
        // A non-aliased file is a bare `UploadFile` (no `File()` call), avoiding
        // ruff B008 (which exempts `Query`/`Header`/`Form` but NOT `File`).
        let needs_multipart = self
            .check_result
            .endpoints
            .iter()
            .any(|ep| ep.body_is_multipart);
        let multipart_fields = || {
            self.check_result
                .endpoints
                .iter()
                .filter(|ep| ep.body_is_multipart)
                .filter_map(|ep| ep.body.as_ref())
                .flat_map(|body| body.fields.iter())
        };
        let needs_file = multipart_fields().any(|f| {
            let is_file = matches!(&f.ty, Type::File);
            let is_opt_file = matches!(
                &f.ty,
                Type::Generic(name, args)
                    if name == "Option" && matches!(args.first(), Some(Type::File))
            );
            (is_file || is_opt_file) && to_snake_case(&f.name) != f.name
        });
        // A path param whose Python identifier diverges from the wire `{pp}` segment
        // (camelCase or a keyword) binds via `Annotated[str, Path(alias=...)]`.
        let needs_path = self
            .check_result
            .endpoints
            .iter()
            .any(|ep| ep.path_params.iter().any(|pp| to_snake_case(pp) != *pp));
        let needs_annotated = needs_file || needs_path;
        let needs_form = multipart_fields().any(|f| {
            let is_file = matches!(&f.ty, Type::File);
            let is_opt_file = matches!(
                &f.ty,
                Type::Generic(name, args)
                    if name == "Option" && matches!(args.first(), Some(Type::File))
            );
            !is_file && !is_opt_file
        });
        // A binary download response, or any endpoint with response headers,
        // needs FastAPI's `Response` (the former returns `Response(content=...)`,
        // the latter sets headers on an injected `Response` param).
        // A multi-status route also returns a `Response` directly (the status is
        // handler-chosen, so FastAPI cannot infer it from a decorator), so it
        // pulls in `Response` too.
        let needs_response = self.check_result.endpoints.iter().any(|ep| {
            !ep.response_headers.is_empty()
                || ep.response_is_binary
                || !ep.response_statuses.is_empty()
        });
        // Standard-library imports form the first group (isort order:
        // datetime < decimal < typing < uuid), emitted before the third-party
        // fastapi group. The `Annotated[...]` file-alias form lives in typing.
        if needs_datetime {
            self.server_out.push_str("from datetime import datetime\n");
        }
        if needs_decimal {
            self.server_out.push_str("from decimal import Decimal\n");
        }
        if needs_annotated {
            self.server_out.push_str("from typing import Annotated\n");
        }
        if needs_uuid {
            self.server_out.push_str("from uuid import UUID\n");
        }
        if needs_datetime || needs_decimal || needs_annotated || needs_uuid {
            self.server_out.push('\n');
        }
        // Keep this list alphabetical so it satisfies ruff's isort (I) ordering.
        let mut names = vec!["APIRouter"];
        if needs_file {
            names.push("File");
        }
        if needs_form {
            names.push("Form");
        }
        if needs_header {
            names.push("Header");
        }
        if needs_http_exception {
            names.push("HTTPException");
        }
        if needs_path {
            names.push("Path");
        }
        if needs_query {
            names.push("Query");
        }
        if needs_response {
            names.push("Response");
        }
        if needs_multipart {
            names.push("UploadFile");
        }
        // Use the shared wrapping emitter so the line folds into the parenthesized
        // multi-line form when it exceeds black's line length (the names already
        // arrive alphabetized, which the helper's case-insensitive sort preserves).
        // Widen the `&'static str` names to `String` for the `&[String]` helper
        // signature (a clone of each literal, not a reallocation of the list).
        let names: Vec<String> = names.into_iter().map(String::from).collect();
        self.server_out
            .push_str(&format_from_import("fastapi", &names));
        self.server_out.push('\n');

        let mut model_imports = BTreeSet::new();
        for ep in &self.check_result.endpoints {
            // A multipart body is exploded into per-field FastAPI params, so the
            // `XBody` pydantic model is never referenced by the route.
            if ep.body.is_some() && !ep.body_is_multipart {
                model_imports.insert(format!("{}Body", capitalize(&ep.name)));
            }
            // A paginated route is annotated `-> <Endpoint>Page` and returns the
            // handler's page through unchanged; the bare item type is referenced
            // only inside that model in models.py, so import the page model and
            // not the item type (an unused item import would trip ruff F401).
            // A multi-status route returns a `Response` built from the envelope's
            // `result.status`/`result.body`; the route never names the body type
            // (it serializes via `result.body.model_dump_json(by_alias=True)`), so
            // importing it here would trip ruff F401. Nothing to import for this branch.
            if !ep.response_statuses.is_empty() {
                // intentionally no model import
            } else if ep.pagination.is_some() {
                model_imports.insert(page_type_name(ep));
            } else if let Some(ref resp) = ep.response
                && !ep.response_is_binary
            {
                // A binary download returns `Response(content=bytes)`; the
                // response struct is never constructed, so its type is not
                // imported.
                collect_python_imports(resp, &mut model_imports);
            }
            // The route returns `result.body`, so the envelope type itself is not
            // imported; only the body's component types (above) are referenced.
            // Enum query/request-header param types ARE named in the route
            // signature (FastAPI validates them), so import those.
            for q in &ep.query_params {
                collect_python_imports(&q.ty, &mut model_imports);
            }
            for h in &ep.headers {
                collect_python_imports(&h.ty, &mut model_imports);
            }
        }
        self.server_out.push_str("from .handlers import Handlers\n");
        if !model_imports.is_empty() {
            let joined: Vec<_> = model_imports.into_iter().collect();
            self.server_out
                .push_str(&format_from_import(".models", &joined));
        }
    }

    /// Emits a single FastAPI route for an endpoint.
    fn emit_server_route(&mut self, ep: &EndpointInfo) {
        let method = ep.method.as_lower_str();
        let fn_name = to_snake_case(&ep.name);
        // A binary download route returns a FastAPI `Response` (the raw bytes
        // wrapped with `media_type="application/octet-stream"`), not the pydantic
        // model; all other routes return the modelled response type.
        let is_multi_status = !ep.response_statuses.is_empty();
        let response_type = if is_multi_status {
            // A multi-status route writes the handler-chosen status dynamically, so
            // it returns a FastAPI `Response` object directly (not the pydantic
            // envelope) — the idiomatic way to control the status at runtime.
            "Response".to_string()
        } else if ep.response_is_binary {
            "Response".to_string()
        } else if ep.pagination.is_some() {
            // The route returns the handler's `<Endpoint>Page` directly (no
            // unwrapping like the response-header `result.body` case).
            page_type_name(ep)
        } else {
            ep.response
                .as_ref()
                .map(type_to_python)
                .unwrap_or_else(|| "None".to_string())
        };

        // FastAPI path: {id} stays as {id} (FastAPI uses same syntax). A
        // multi-status route sets the status dynamically inside the body (via the
        // returned `Response`), so no static `status_code=` on the decorator.
        let status = if !is_multi_status && ep.response.is_none() {
            ", status_code=204"
        } else {
            ""
        };

        ensure_blank_lines(&mut self.server_out, 1);
        self.server_out.push_str(&format!(
            "    @router.{}(\"{}\"{})\n",
            method, ep.path, status
        ));

        // Build function params. Python requires every parameter WITHOUT a
        // default to precede those WITH one, so we collect into two ordered
        // groups and concatenate. Path params and the body never carry a
        // default; query params land in `defaulted` when they render a `= ...`
        // (optional params, and required-but-aliased params — see below).
        let mut required = Vec::new();
        let mut defaulted = Vec::new();
        for pp in &ep.path_params {
            let snake = to_snake_case(pp);
            // When the Python identifier diverges from the wire `{pp}` segment — a
            // camelCase param (`{postId}` → `post_id`) or a keyword (`{class}` →
            // `class_`) — FastAPI matches path params by the function-parameter name,
            // so the binding needs `Path(alias="<wire>")` or it never binds (a 422 at
            // runtime). The `Annotated` form keeps it a non-defaulted param (path
            // params precede the body, which has no default). A param that is already
            // identical (`{id}` → `id`) stays a plain `str`, unchanged.
            if snake != *pp {
                required.push(format!("{snake}: Annotated[str, Path(alias=\"{pp}\")]"));
            } else {
                required.push(format!("{snake}: str"));
            }
        }
        if ep.body_is_multipart {
            // Multipart: explode the body struct into per-field FastAPI params.
            //
            // FILE fields: FastAPI auto-detects an `UploadFile`-typed param as a
            // multipart file, so a non-aliased file needs NO marker — bare
            // `UploadFile` (required, no default → `required`) / `UploadFile |
            // None = None` (optional → `defaulted`). This deliberately avoids a
            // `File(...)` default: ruff's B008 immutable-call allowlist covers
            // `Query`/`Header`/`Form` but NOT `fastapi.File`, so `File(...)` as a
            // default trips B008. When the wire name differs from the snake_case
            // param we still need an alias; that goes in the *annotation* via
            // `Annotated[UploadFile, File(alias="<wire>")]` — a call inside an
            // annotation, not in default position, so B008 never fires.
            //
            // SCALAR fields: bind via `Form(...)`/`Form(None)` (FastAPI's
            // form-field marker, on ruff's B008 allowlist), with `alias=` when the
            // wire name differs — mirroring the query/header path. Both are
            // syntactic defaults, so they sort into `defaulted`.
            let body = ep
                .body
                .as_ref()
                .expect("multipart body has resolved fields");
            for f in &body.fields {
                let snake = to_snake_case(&f.name);
                let needs_alias = snake != f.name;
                let alias = if needs_alias {
                    format!("alias=\"{}\"", f.name)
                } else {
                    String::new()
                };
                let is_file = matches!(&f.ty, Type::File);
                let is_opt_file = matches!(
                    &f.ty,
                    Type::Generic(name, args)
                        if name == "Option" && matches!(args.first(), Some(Type::File))
                );
                // A `File` field relaxed by `partial` (`f.optional`) is an
                // optional upload, exactly like `Option<File>`.
                if is_file && !f.optional {
                    let ann = if needs_alias {
                        format!("Annotated[UploadFile, File({alias})]")
                    } else {
                        "UploadFile".to_string()
                    };
                    required.push(format!("{snake}: {ann}"));
                } else if is_opt_file || is_file {
                    let ann = if needs_alias {
                        format!("Annotated[UploadFile | None, File({alias})]")
                    } else {
                        "UploadFile | None".to_string()
                    };
                    defaulted.push(format!("{snake}: {ann} = None"));
                } else {
                    let py_type = type_to_python(&f.ty);
                    // `partial`/`Option<T>` scalar form fields are optional.
                    let already_optional =
                        matches!(&f.ty, Type::Generic(name, _) if name == "Option");
                    let alias_arg = if needs_alias {
                        format!(", {alias}")
                    } else {
                        String::new()
                    };
                    // `where`-constraints are enforced inline in the route body (the
                    // multipart-constraint block below), over the FULL expression —
                    // so `Form(...)` carries only the alias. This replaces the old
                    // extractable-only `Form(min_length=…)` kwargs, which silently
                    // dropped non-numeric/length constraints (e.g. `contains`),
                    // mirroring the JSON body's move to a model_validator.
                    if f.optional || already_optional {
                        let ty = if f.optional && !already_optional {
                            format!("{py_type} | None")
                        } else {
                            py_type
                        };
                        defaulted.push(format!("{snake}: {ty} = Form(None{alias_arg})"));
                    } else {
                        defaulted.push(format!("{snake}: {py_type} = Form(...{alias_arg})"));
                    }
                }
            }
        } else if ep.body.is_some() {
            let body_type = format!("{}Body", capitalize(&ep.name));
            required.push(format!("body: {body_type}"));
        }
        for qp in &ep.query_params {
            let py_type = type_to_python(&qp.ty);
            let snake = to_snake_case(&qp.name);
            // A list-valued query param: FastAPI parses repeated keys into a
            // `list[T]` only when bound via `Query(...)` (a bare `list[T]` would be
            // read as a body), so always emit `Query`. An absent param → empty list.
            if matches!(&qp.ty, Type::Generic(n, _) if n == "List") {
                let needs_alias = snake != qp.name;
                let alias = if needs_alias {
                    format!(", alias=\"{}\"", qp.name)
                } else {
                    String::new()
                };
                defaulted.push(format!(
                    "{snake}: {py_type} = Query(default_factory=list{alias})"
                ));
                continue;
            }
            let is_optional =
                qp.has_default || matches!(&qp.ty, Type::Generic(name, _) if name == "Option");
            // FastAPI matches query keys to the Python parameter name. The
            // generated client sends the schema's original (camelCase) name, so
            // when snake_case differs we must pin the wire key with an explicit
            // `Query(default, alias="<wireName>")` — otherwise FastAPI never sees
            // the value and silently falls back to the default. Single-word
            // params (snake == wire) need no alias and stay plain.
            let needs_alias = snake != qp.name;
            if is_optional {
                let default = qp
                    .default_value
                    .as_ref()
                    .map(|d| py_default_for(d, &py_type))
                    .unwrap_or_else(|| "None".to_string());
                if needs_alias {
                    defaulted.push(format!(
                        "{snake}: {py_type} = Query({default}, alias=\"{}\")",
                        qp.name
                    ));
                } else {
                    defaulted.push(format!("{snake}: {py_type} = {default}"));
                }
            } else if needs_alias {
                // A required aliased param renders `= Query(alias=...)`, which is
                // a syntactic default, so it must sort after the non-defaulted
                // params even though FastAPI still treats it as required (a
                // `Query(...)` with no value sentinel is required regardless of
                // position).
                defaulted.push(format!("{snake}: {py_type} = Query(alias=\"{}\")", qp.name));
            } else {
                required.push(format!("{snake}: {py_type}"));
            }
        }
        // Request headers bind via FastAPI `Header(default, alias="<wire_name>")`.
        // The wire name (e.g. `Idempotency-Key`) is never a valid Python
        // identifier, so headers ALWAYS need the alias — there is no plain form.
        // `Header(...)` is a syntactic default, so every header lands in
        // `defaulted` (after the non-defaulted path/body/response params).
        for hp in &ep.headers {
            let py_type = type_to_python(&hp.ty);
            let snake = to_snake_case(&hp.name);
            // A list-valued request header is comma-separated: FastAPI can't split
            // it, so receive the raw `str` (default "" = absent) and split it into
            // a `list[T]` in the route body before calling the handler. The same
            // `header_ty_is_list` predicate gates the body split + the handler arg.
            if header_ty_is_list(&hp.ty) {
                defaulted.push(format!(
                    "{snake}: str = Header(default=\"\", alias=\"{}\")",
                    hp.wire_name
                ));
                continue;
            }
            let is_optional =
                hp.has_default || matches!(&hp.ty, Type::Generic(name, _) if name == "Option");
            if is_optional {
                let default = hp
                    .default_value
                    .as_ref()
                    .map(|d| py_default_for(d, &py_type))
                    .unwrap_or_else(|| "None".to_string());
                defaulted.push(format!(
                    "{snake}: {py_type} = Header({default}, alias=\"{}\")",
                    hp.wire_name
                ));
            } else {
                // Required-but-aliased: `Header(alias=...)` (no value sentinel) is
                // required regardless of position, mirroring the required `Query`
                // alias form above.
                defaulted.push(format!(
                    "{snake}: {py_type} = Header(alias=\"{}\")",
                    hp.wire_name
                ));
            }
        }
        // A `response: Response` param lets the route set response headers; it
        // has no default, so it sorts with the other required params.
        let has_response_headers = !ep.response_headers.is_empty();
        if has_response_headers {
            required.push("response: Response".to_string());
        }
        let params: Vec<String> = required.into_iter().chain(defaulted).collect();

        let sig = format_def_signature(
            "    ",
            "async def",
            &fn_name,
            &params,
            &format!(" -> {response_type}:"),
        );
        self.server_out.push_str(&sig);

        // Multipart scalar `where`-constraints: FastAPI's `Form(...)` no longer
        // carries the constraint, so enforce the FULL expression inline (→ 422),
        // matching the JSON body's model_validator and Go/TS. Emitted before any
        // error-handling `try:`, so a violation is a 422 regardless of the
        // handler's error mapping. Constraints only ever sit on scalar fields.
        if ep.body_is_multipart
            && let Some(body) = ep.body.as_ref()
        {
            // `slot` indexes the constrained fields (not all fields), so the unique
            // snapshot local each guard may emit (`value<slot>`) stays collision-free.
            for (slot, f) in body
                .fields
                .iter()
                .filter(|f| f.constraint.is_some())
                .enumerate()
            {
                let constraint = f.constraint.as_ref().expect("filtered to Some above");
                let snake = to_snake_case(&f.name);
                let is_optional =
                    f.optional || matches!(&f.ty, Type::Generic(name, _) if name == "Option");
                let detail = format!("{}: constraint not satisfied", f.name);
                push_constraint_guard(
                    &mut self.server_out,
                    "        ",
                    is_optional,
                    constraint,
                    &snake,
                    slot,
                );
                push_http_exception_422(&mut self.server_out, "            ", &detail);
            }
        }

        // Error handling wrapper
        let has_errors = !ep.errors.is_empty();
        let indent = if has_errors {
            "            "
        } else {
            "        "
        };

        if has_errors {
            self.server_out.push_str("        try:\n");
        }

        // A list-valued request header arrives as one comma-separated `str` param;
        // split + trim + coerce each element into a `{snake}_items` local the handler
        // call below consumes by that exact name (see the matching `_items` branch in
        // the handler-arg loop) — the two stay in lockstep on the `List` predicate.
        // Emitted at `indent`, so when the endpoint declares errors this sits inside
        // the `try:` — a malformed element (`int(...)`/`UUID(...)`/`Enum(...)`) then
        // raises, runs through the `except` error-name map below (matching nothing),
        // and re-raises → 500. That's the accepted query-vs-header divergence (see
        // design-decisions.md), not a bug: FastAPI can't split a comma header into a
        // typed `list[T]`, so the coercion is ours and its failure is a 500.
        for hp in &ep.headers {
            if !header_ty_is_list(&hp.ty) {
                continue;
            }
            let snake = to_snake_case(&hp.name);
            let elem = crate::unwrap_option_or_list(&hp.ty);
            let elem_py = type_to_python(elem);
            let coerce = py_elem_coerce(elem, "stripped");
            self.server_out.push_str(&format!(
                "{indent}{snake}_items: list[{elem_py}] = []\n{indent}for part in {snake}.split(\",\"):\n{indent}    stripped = part.strip()\n{indent}    if stripped:\n{indent}        {snake}_items.append({coerce})\n"
            ));
        }

        // Call handler
        let mut handler_args = Vec::new();
        for pp in &ep.path_params {
            let snake = to_snake_case(pp);
            handler_args.push(format!("{snake}={snake}"));
        }
        if ep.body_is_multipart {
            // Pass each exploded multipart field through to the handler by name.
            let body = ep
                .body
                .as_ref()
                .expect("multipart body has resolved fields");
            for f in &body.fields {
                let snake = to_snake_case(&f.name);
                handler_args.push(format!("{snake}={snake}"));
            }
        } else if ep.body.is_some() {
            handler_args.push("body=body".to_string());
        }
        for qp in &ep.query_params {
            let snake = to_snake_case(&qp.name);
            handler_args.push(format!("{snake}={snake}"));
        }
        for hp in &ep.headers {
            let snake = to_snake_case(&hp.name);
            // A list header was split into `{snake}_items` by the coercion loop
            // above; both sites share `header_ty_is_list` so the local and its
            // consumer can never drift apart (the `_items` local exists only when
            // this holds).
            if header_ty_is_list(&hp.ty) {
                handler_args.push(format!("{snake}={snake}_items"));
            } else {
                handler_args.push(format!("{snake}={snake}"));
            }
        }

        if is_multi_status {
            // The handler returns the `<Endpoint>Response` envelope carrying the
            // chosen status + optional shared body. The route writes that status
            // (not a static 200/204) by returning a `Response` directly: serialize
            // `result.body` as JSON when present, else an empty-body response with
            // the chosen status. (An all-typeless block has no `body` field, so the
            // response is always bodyless.)
            //
            // The handler-chosen envelope is validated against the DECLARED
            // contract first — all three mismatches are handler bugs, reported as
            // a 500 instead of written to the wire (mirrors the Go/TS servers):
            // - an undeclared status (a buggy handler returning 0, or a 4xx
            //   smuggled through the success envelope, bypassing `error { }`);
            // - a body paired with a typeless status (Starlette only suppresses
            //   bodies on 204/304 — on e.g. a typeless 202 the body WOULD hit the
            //   wire, and the content-guarded client would parse it, silently
            //   violating the contract);
            // - a None body paired with a typed status (the contract — and the
            //   OpenAPI spec — promise a body there).
            // An all-typeless block has no `body` field, so only the membership
            // check applies.
            let call = format_call(
                indent,
                &format!("result = await handlers.{fn_name}"),
                &handler_args,
                "",
            );
            self.server_out.push_str(&call);
            // Each guard's `return Response(...)` goes through `format_call` so
            // the wrapping tracks the route's actual depth the way black does
            // (collapsed args at shallow indents, exploded with a trailing
            // comma once the collapsed line passes 88 columns).
            //
            // KNOWN BOUND: the `if <condition>:` lines themselves are NOT
            // width-aware. A block declaring roughly ≥7-8 statuses pushes the
            // status-tuple membership test past 88 columns, where black would
            // parenthesize and re-wrap the condition — so that generated server
            // would fail a user's `black --check`. Accepted for now: realistic
            // blocks declare 2-4 statuses, and the fix (a width-aware condition
            // emitter mirroring black's wrapping of boolean ops) is not worth
            // its complexity until someone hits the bound.
            let emit_500 = |out: &mut String, condition: &str, message: &str| {
                out.push_str(&format!("{indent}if {condition}:\n"));
                out.push_str(&format_call(
                    &format!("{indent}    "),
                    "return Response",
                    &[
                        format!("content=\"{message}\""),
                        "status_code=500".to_string(),
                    ],
                    "",
                ));
            };
            emit_500(
                &mut self.server_out,
                &format!("result.status not in {}", declared_statuses_py(ep)),
                "handler returned undeclared status",
            );
            if ep.response.is_some() {
                let typed: Vec<u16> = ep
                    .response_statuses
                    .iter()
                    .filter(|rs| rs.ty.is_some())
                    .map(|rs| rs.status)
                    .collect();
                let typeless: Vec<u16> = ep
                    .response_statuses
                    .iter()
                    .filter(|rs| rs.ty.is_none())
                    .map(|rs| rs.status)
                    .collect();
                emit_500(
                    &mut self.server_out,
                    &format!(
                        "result.status in {} and result.body is None",
                        py_status_tuple(&typed)
                    ),
                    "handler returned no body for a typed status",
                );
                if !typeless.is_empty() {
                    emit_500(
                        &mut self.server_out,
                        &format!(
                            "result.status in {} and result.body is not None",
                            py_status_tuple(&typeless)
                        ),
                        "handler returned a body for a bodyless status",
                    );
                }
                self.server_out.push_str(&format!(
                    "{indent}if result.body is not None:\n{indent}    return Response(\n{indent}        content=result.body.model_dump_json(by_alias=True),\n{indent}        status_code=result.status,\n{indent}        media_type=\"application/json\",\n{indent}    )\n{indent}return Response(status_code=result.status)\n"
                ));
            } else {
                self.server_out.push_str(&format!(
                    "{indent}return Response(status_code=result.status)\n"
                ));
            }
        } else if has_response_headers {
            // The handler returns the typed envelope; capture it, copy each
            // response header onto the FastAPI `Response` (string-coerced, guarded
            // for optional), then return the bare body for serialization.
            let call = format_call(
                indent,
                &format!("result = await handlers.{fn_name}"),
                &handler_args,
                "",
            );
            self.server_out.push_str(&call);
            for hp in &ep.response_headers {
                let snake = to_snake_case(&hp.name);
                // Response-header optionality is `Option<T>` ONLY (a `= default`
                // is meaningless for a response header), matching the envelope.
                let is_optional = matches!(&hp.ty, Type::Generic(name, _) if name == "Option");
                let inner = unwrap_option(&hp.ty);
                // Stringify for the wire. `str` passes through; `bool` is emitted
                // as lowercase `true`/`false` so it round-trips with the client's
                // `== "true"` read (Python's `str(True)` would yield "True").
                let value = match &inner {
                    Type::String => format!("result.{snake}"),
                    Type::Bool => format!("\"true\" if result.{snake} else \"false\""),
                    // RFC 3339 on the wire (guarded by `is not None` when optional).
                    Type::DateTime => format!("result.{snake}.isoformat()"),
                    // An enum is a `(str, Enum)` — write its `.value` (the bare
                    // variant), not `str(enum)` which emits `Color.Red`.
                    Type::Named(_) => format!("result.{snake}.value"),
                    _ => format!("str(result.{snake})"),
                };
                if is_optional {
                    self.server_out.push_str(&format!(
                        "{indent}if result.{snake} is not None:\n{indent}    response.headers[\"{}\"] = {value}\n",
                        hp.wire_name
                    ));
                } else {
                    self.server_out.push_str(&format!(
                        "{indent}response.headers[\"{}\"] = {value}\n",
                        hp.wire_name
                    ));
                }
            }
            self.server_out
                .push_str(&format!("{indent}return result.body\n"));
        } else if ep.response_is_binary {
            // Binary download: the handler returns the raw `bytes`; wrap them in a
            // FastAPI `Response` with an octet-stream media type. (A binary
            // response struct is exactly one `File` field — never combined with
            // response headers — so this branch is independent of those.)
            let call = format_call(
                indent,
                &format!("data = await handlers.{fn_name}"),
                &handler_args,
                "",
            );
            self.server_out.push_str(&call);
            self.server_out.push_str(&format!(
                "{indent}return Response(content=data, media_type=\"application/octet-stream\")\n"
            ));
        } else {
            let prefix = if ep.response.is_some() {
                format!("return await handlers.{fn_name}")
            } else {
                format!("await handlers.{fn_name}")
            };
            let call = format_call(indent, &prefix, &handler_args, "");
            self.server_out.push_str(&call);
        }

        // Error mapping
        if has_errors {
            self.server_out.push_str("        except Exception as e:\n");
            for (name, code) in &ep.errors {
                self.server_out
                    .push_str(&format!("            if str(e) == \"{name}\":\n"));
                // `raise HTTPException(...) from e` wraps like black once a long
                // error name pushes it past 88 columns.
                let raise = format_call(
                    "                ",
                    "raise HTTPException",
                    &[format!("status_code={code}"), format!("detail=\"{name}\"")],
                    " from e",
                );
                self.server_out.push_str(&raise);
            }
            self.server_out.push_str("            raise\n");
        }
    }
}


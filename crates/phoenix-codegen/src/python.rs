/// Strips a single `Option<T>` wrapper, returning the inner type; non-option
/// types are returned unchanged. Used to find the scalar type carried by a
/// response header so the client can coerce the wire string to it.
fn unwrap_option(ty: &Type) -> Type {
    match ty {
        Type::Generic(name, args) if name == "Option" && args.len() == 1 => args[0].clone(),
        other => other.clone(),
    }
}

/// The Python expression that decodes a JSON-native value `src` (e.g.
/// `response.json()` or a comprehension variable) into the in-memory
/// representation of `ty`. The expression is always explicitly typed (never a
/// bare `response.json()` pass-through) so it satisfies mypy strict mode's
/// `warn_return_any`:
/// - a scalar uses its constructor (`int`/`float`/`bool`/`str`); a `DateTime`
///   parses the RFC 3339 string via `datetime.fromisoformat`;
/// - a `Named` struct/enum is built via `Model(**src)` — pydantic coerces nested
///   fields, including ISO datetime strings, so nested `DateTime`s round-trip;
/// - `List`/`Map` rebuild element-wise; `Option` guards `None`.
///
/// This replaces the old `Type(**response.json())` form, which only worked when
/// the response was an object (or list of objects) and crashed at runtime for a
/// scalar/scalar-collection response (e.g. `response DateTime` →
/// `datetime(**"…")`). See `docs/design-decisions.md` (DateTime & UUID scalar
/// types).
fn py_decode_expr(ty: &Type, src: &str) -> String {
    match ty {
        Type::Int => format!("int({src})"),
        Type::Float => format!("float({src})"),
        Type::Bool => format!("bool({src})"),
        Type::String => format!("str({src})"),
        Type::DateTime => format!("datetime.fromisoformat({src})"),
        Type::Named(name) => format!("{name}(**{src})"),
        Type::Generic(name, args) if name == "List" && args.len() == 1 => {
            format!("[{} for item in {src}]", py_decode_expr(&args[0], "item"))
        }
        Type::Generic(name, args) if name == "Map" && args.len() == 2 => {
            format!(
                "{{k: {} for k, v in {src}.items()}}",
                py_decode_expr(&args[1], "v")
            )
        }
        Type::Generic(name, args) if name == "Option" && args.len() == 1 => {
            format!(
                "{} if {src} is not None else None",
                py_decode_expr(&args[0], src)
            )
        }
        // Any other type (e.g. `File`, never a response body) passes through.
        _ => src.to_string(),
    }
}

/// Whether a type's generated Python references `datetime` (directly, or nested
/// inside `Option`/`List`/`Map`), and so requires the
/// `from datetime import datetime` import in the file that renders it. NOT
/// transitive through `Named` structs on purpose: import detection is per-file,
/// and a nested struct names `datetime` in its own model, not in the file that
/// merely references it. For "does this value's JSON dump contain a datetime
/// anywhere", use [`type_uses_datetime_deep`].
fn type_uses_datetime(ty: &Type) -> bool {
    match ty {
        Type::DateTime => true,
        Type::Generic(_, args) => args.iter().any(type_uses_datetime),
        _ => false,
    }
}

/// Like [`type_uses_datetime`], but transitively follows `Named` struct fields
/// (resolved via `check_result`), so a value that reaches a `DateTime` only
/// through a nested struct is still detected. Used to decide
/// `model_dump(mode="json")`: pydantic's default dump leaves nested `datetime`
/// objects that httpx's `json.dumps` rejects, so any reachable `DateTime` must
/// force JSON mode. `visited` guards against recursive struct definitions.
fn type_uses_datetime_deep(
    ty: &Type,
    check_result: &Analysis,
    visited: &mut BTreeSet<String>,
) -> bool {
    match ty {
        Type::DateTime => true,
        Type::Generic(_, args) => args
            .iter()
            .any(|a| type_uses_datetime_deep(a, check_result, visited)),
        Type::Named(name) => {
            // A struct seen on the current path can't introduce a new
            // `DateTime`; returning false breaks the cycle.
            if !visited.insert(name.clone()) {
                return false;
            }
            check_result
                .module
                .struct_info_by_name(name)
                .is_some_and(|info| {
                    info.fields
                        .iter()
                        .any(|f| type_uses_datetime_deep(&f.ty, check_result, visited))
                })
        }
        _ => false,
    }
}

/// The Python name of the pagination envelope model for an endpoint
/// (e.g. `listPosts` → `ListPostsPage`). Only emitted/used when the endpoint
/// declares a `pagination { }` block. Distinct from the `<Endpoint>Result`
/// response-header envelope (pagination and response headers are mutually
/// exclusive — sema rejects the combination).
fn page_type_name(ep: &EndpointInfo) -> String {
    format!("{}Page", capitalize(&ep.name))
}

/// The Python name of the multi-status response envelope model for an endpoint
/// (e.g. `upsertUser` → `UpsertUserResponse`). Only emitted/used when the
/// endpoint declares a `response { }` block (`response_statuses` non-empty).
/// Distinct from the `<Endpoint>Result` response-header envelope and the
/// `<Endpoint>Page` pagination envelope (all three are mutually exclusive — sema
/// rejects the combinations).
fn multi_status_type_name(ep: &EndpointInfo) -> String {
    format!("{}Response", capitalize(&ep.name))
}

/// Renders the declared success statuses of a multi-status endpoint as a Python
/// tuple literal for the server's handler-status validation (e.g. `(200, 201)`).
fn declared_statuses_py(ep: &EndpointInfo) -> String {
    let codes: Vec<u16> = ep.response_statuses.iter().map(|rs| rs.status).collect();
    py_status_tuple(&codes)
}

/// Renders a list of status codes as a Python tuple literal (a single-element
/// tuple needs the trailing comma: `(201,)`).
fn py_status_tuple(codes: &[u16]) -> String {
    let rendered: Vec<String> = codes.iter().map(|c| c.to_string()).collect();
    if rendered.len() == 1 {
        format!("({},)", rendered[0])
    } else {
        format!("({})", rendered.join(", "))
    }
}

/// Emits the client-side coercion of a single response header from its raw
/// `str | None` wire read (`<snake>_raw`) into the typed local `<snake>` the
/// envelope expects.
///
/// `inner_ty` is the header type with any `Option<…>` stripped. Optional headers
/// stay `None` when absent; required headers coerce directly, fabricating the
/// type's zero value only to satisfy the type checker (a required response header
/// is contractually always present on the wire).
fn header_read_coercion(inner_ty: &Type, snake: &str, is_optional: bool) -> String {
    let raw = format!("{snake}_raw");
    let rhs = if is_optional {
        // None/empty reads as `None`; a present value is parsed to the inner
        // scalar via a truthiness ternary (`is not None` would be longer).
        match inner_ty {
            Type::Int => format!("int({raw}) if {raw} else None"),
            Type::Float => format!("float({raw}) if {raw} else None"),
            Type::Bool => format!("({raw} == \"true\") if {raw} else None"),
            Type::DateTime => format!("datetime.fromisoformat({raw}) if {raw} else None"),
            // `str` (and any unmodeled scalar) passes through untouched.
            _ => raw.clone(),
        }
    } else {
        // A required response header is contractually present; the `or <zero>`
        // fallback only narrows the `str | None` read to a non-None value for the
        // type checker.
        match inner_ty {
            Type::Int => format!("int({raw} or 0)"),
            Type::Float => format!("float({raw} or 0)"),
            Type::Bool => format!("{raw} == \"true\""),
            // A required response header is contractually present; the epoch
            // fallback only narrows the `str | None` read for the type checker.
            Type::DateTime => {
                format!("datetime.fromisoformat({raw} or \"1970-01-01T00:00:00+00:00\")")
            }
            _ => format!("{raw} or \"\""),
        }
    };
    emit_py_assignment("        ", snake, &rhs)
}

/// Renders a single `lhs = rhs` assignment, wrapping the RHS in parentheses one
/// indent level deeper when the one-line form would exceed [`LINE_LENGTH`] — the
/// layout black produces for an over-long assignment. Keeps generated coercions
/// (whose `lhs`/`rhs` both embed the header's snake_case name, so a long name can
/// push past 88 columns) formatter-clean. Returns the line(s) with a trailing
/// newline.
fn emit_py_assignment(indent: &str, lhs: &str, rhs: &str) -> String {
    let one_line = format!("{indent}{lhs} = {rhs}");
    if one_line.len() <= LINE_LENGTH {
        return format!("{one_line}\n");
    }
    format!("{indent}{lhs} = (\n{indent}    {rhs}\n{indent})\n")
}

/// Renders `text` as one or more Python `#` comments, each prefixed with
/// `indent`. A multi-line doc comment (the lexer joins its lines with `\n`) gets
/// EVERY line prefixed, so continuation lines stay commented instead of leaking
/// into the file as code. Trailing whitespace is trimmed per line, so an empty
/// line renders as a bare `#` (no trailing space) — keeping `# ` block-comment
/// linters (ruff E265/W291) happy — matching the Go `render_line_comment` helper.
fn render_hash_comment(indent: &str, text: &str) -> String {
    let mut out = String::new();
    for line in text.split('\n') {
        out.push_str(format!("{indent}# {line}").trim_end());
        out.push('\n');
    }
    out
}

/// Converts a Phoenix `Type` to a Python type annotation string.
fn type_to_python(ty: &Type) -> String {
    match ty {
        Type::Int => "int".to_string(),
        Type::Float => "float".to_string(),
        Type::String => "str".to_string(),
        Type::Bool => "bool".to_string(),
        // A `File` body field is a binary upload/download. FastAPI's server-side
        // type is `UploadFile`; multipart param wiring (`= File(...)`) and binary
        // responses live in the body-codegen path. This is the field type.
        Type::File => "UploadFile".to_string(),
        // A `DateTime` is an RFC 3339 instant. Pydantic v2 parses/serializes
        // `datetime.datetime` to/from ISO 8601 out of the box, so the field type
        // is `datetime` (the import is added wherever a DateTime appears). See
        // `docs/design-decisions.md` (DateTime & UUID scalar types).
        Type::DateTime => "datetime".to_string(),
        Type::Void => "None".to_string(),
        Type::Named(name) => name.clone(),
        Type::Generic(name, args) if name == "List" && args.len() == 1 => {
            format!("list[{}]", type_to_python(&args[0]))
        }
        Type::Generic(name, args) if name == "Map" && args.len() == 2 => {
            format!(
                "dict[{}, {}]",
                type_to_python(&args[0]),
                type_to_python(&args[1])
            )
        }
        Type::Generic(name, args) if name == "Option" && args.len() == 1 => {
            format!("{} | None", type_to_python(&args[0]))
        }
        Type::Generic(name, args) => {
            let py_args: Vec<String> = args.iter().map(type_to_python).collect();
            format!("{}[{}]", name, py_args.join(", "))
        }
        // Trait objects erase to the trait name (dispatched at runtime via
        // duck typing in Python); sharper Protocol-based mappings can replace
        // this once specific traits need per-trait shims.
        Type::Dyn(name) => name.clone(),
        _ => "object".to_string(),
    }
}

/// Converts a camelCase identifier to snake_case.
fn to_snake_case(s: &str) -> String {
    let mut result = String::new();
    for (i, c) in s.chars().enumerate() {
        if c.is_uppercase() && i > 0 {
            result.push('_');
        }
        result.push(c.to_lowercase().next().unwrap_or(c));
    }
    result
}

/// Converts a PascalCase name to SCREAMING_SNAKE_CASE for Python enum values.
fn to_screaming_snake(s: &str) -> String {
    let mut result = String::new();
    for (i, c) in s.chars().enumerate() {
        if c.is_uppercase() && i > 0 {
            result.push('_');
        }
        result.push(c.to_uppercase().next().unwrap_or(c));
    }
    result
}


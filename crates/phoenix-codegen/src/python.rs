/// Like [`type_uses_datetime`], but transitively follows `Named` struct fields
/// (resolved via `check_result`), so a value that reaches a `DateTime` only
/// through a nested struct is still detected. Used to decide
/// `model_dump(mode="json")`: pydantic's default dump leaves nested `datetime`
/// objects that httpx's `json.dumps` rejects, so any reachable `DateTime` must
/// force JSON mode. `visited` guards against recursive struct definitions.
fn type_uses_datetime_deep(
    ty: &Type,
    check_result: &Analysis,
    visited: &mut BTreeSet<String>,
) -> bool {
    match ty {
        Type::DateTime => true,
        Type::Generic(_, args) => args
            .iter()
            .any(|a| type_uses_datetime_deep(a, check_result, visited)),
        Type::Named(name) => {
            // A struct seen on the current path can't introduce a new
            // `DateTime`; returning false breaks the cycle.
            if !visited.insert(name.clone()) {
                return false;
            }
            check_result
                .module
                .struct_info_by_name(name)
                .is_some_and(|info| {
                    info.fields
                        .iter()
                        .any(|f| type_uses_datetime_deep(&f.ty, check_result, visited))
                })
        }
        _ => false,
    }
}

